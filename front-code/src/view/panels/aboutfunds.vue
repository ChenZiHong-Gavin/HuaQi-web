            <h1>1. 股票多空策略</h1>
            <p>股票多空是对冲基金最常使用，也是目前管理规模最大的一种策略，它是通过多头和空头的组合，来减少市场波动的风险。与纯多头策略相比，多空策略虽然也是使用股票，但事实上却完全不同：它是传统权益资产的延伸，或者可以看做是一种新的投资组合；成立和管理一支多空对冲基金也复杂的多。</p>
            <p style="font-weight:900">1.1 策略分析</p>
            <p>假设一支对冲基金拥有1000元投资资金，基金经理发现了被低估的股票A和被高估的股票B，于是他采取了以下操作：</p>
            <p>1.把1000元资金存入托管经纪人处，买入900元股票A，剩余现金头寸100元。这个过程并没有杠杆，净多头风险敞口为90%，总风险敞口为权益资本的</p>
            <p>90%。</p>
            <p>2.借入800元股票B，并卖空，在到期日的时候再支付一笔租借费用。</p>
            <p>此时的投资组合包含了900元A股票多头，800元B股票空头和900元的现金多头，累计资产为2600元，对比1000元的权益资本，杠杆率为2.6。换句话说，该投资组合带有90%多头敞口、80%空头敞口、170%的总敞口和10%的净敞口。</p>
            <p>多空策略取得绝对收益，主要依靠的还是多头（空头）业绩战胜空头（多头）的相对情况。换句话说，不管对于牛市或者熊市，或者即使基金经理判断错误了市场的方向，但是依然可以通过两个头寸的相对业绩，带来正向收益。</p>
            <p>创建多空策略头寸的关键是卖空的能力，或者说是低成本借入股票的能力。实践中，买入900元股票A和卖出900元股票B，可以创建一个美元中性头寸；更复杂一些的策略中，会通过多空组合的beta建立beta中性头寸，这也是股票市场中性的基金经常使用的策略。</p>
            <p>一般来说，多头组合的风险值比多头组合小很多，主要是因为更好地分散。两只股票之间存在高相关性的概率是比较大的，在多头组合中，这样的组合并不能良好地分散风险；而在多空组合中，多头头寸和空头头寸就变成了负相关性，可以很大程度提高分散的有效性。而为了减少选股错误带来的损失，基金经理也会最大程度分散头寸（如100到200个头寸），减少持仓集中度（如最高5%）。</p>
            <p> </p>
            <p style="font-weight:900">1.2 收益来源</p>
            <p>首先是多头和空头业绩的差值。当然，最理想状况是多头上涨，空头下跌，带来双倍Alpha的收益；然后是卖空抵押物带来的利息回扣；其次是那些流动性好的准备金的利率；再次是多头和空头之间的红利差。</p>
            <p> </p>
            <p style="font-weight:900">1.3 空头作用</p>
            <p>1. 对于高估值的、预期将来会下跌的股票的押注</p>
            <p>2. 用于对冲多头的市场风险</p>
            <p>3. 可以少量地获得短期的利息收益</p>
            <p> </p>
            <p style="font-weight:900">1.4 多空策略的缺点</p>
            <p>1.  高额的交易费用：总风险敞口经常大于初始资本，而交易成本是基于初始资本的百分比来计算的。减少费用的办法是降低敞口，比如用50%的初始资本来进行交易。</p>
            <p>2. 高换手率：由于多空头寸的市值始终变化，需要经常调整组合，以维持平衡状态；另一方面，为了避免保证金处罚，可能需要一些额外的交易。</p>
            <p>3. 执行延迟：很多交易所只允许在股票价格上涨或持平的时候做空。</p>
            <p>4. 牛市滞后：多空策略无法计算权益风险溢价，尤其是在牛市中，空头作为对冲头寸，减少了市场风险。</p>
            <p>5. 净多头偏差：许多多空策略基金都是偏多头，主要原因包括两个方面，一是该策略的基金经理大多数是纯多头背景，不太适应做空；二是建立好多空的平衡之后，由于多头价值上升，空头价值下跌，导致净头寸的偏移。</p>
            <p> </p>
            <p style="font-weight:900">2. 单向做空策略</p>
            <p>专门进行做空交易的对冲基金策略的基金在市场上寻找那些被高估的公司，借入这些公司的股票并卖出，等待这些股价下跌，低价买入而盈利。纯空头策略曾经是对冲基金一个稳定的策略，但是90年代的牛市让许多这类基金破产，之后大多数这类策略就转变为多空或者偏空头策略。</p>
            <p> </p>
            <p style="font-weight:900">2.1 策略特点</p>
            <p> </p>
            <p>和市场上挖掘低估值的股票相反，卖空的机会远远没有被完全挖掘。大多数投资人还是习惯于买入 - 持有的策略，而对于卖空交易不熟悉，并且觉得风险过高。一个很明显的例子是，分析师总是对公司进行正面的评价，即使是表现很差的公司，最多也是只是“保持”或“减持”的建议。当然，这主要也是因为分析师和上市公司之间利益冲突的问题，如果给了过于负面的评价，那么他很可能因此无法再获取到这家公司的信息，甚至失去这个客户。</p>
            <p>虽然看起来卖空策略有更多的机会，但是这个策略要挣钱并不容易。</p>
            <p>首先，纯多头策略的投资人可以长期持有股票，赚取市场风险溢价和股票红利；而纯空头策略，一方面得面对长期上涨的市场趋势的影响，支付股利；另一方面还随时面临买入这些股票归还券商的风险。</p>
            <p>其次，对于持有小盘股和低流动性的股票空头，还面临着滚雪球式的买入风险，导致空头挤压。简单说，就是一旦股票价格上升，空头就不得不开始买入股票平仓，导致股价更快地上涨，然后更多的空头遭遇损失……</p>
            <p>再次，纯空头策略具有很高的风险，因为价格上涨的空间是无限的，而下跌的空间是有限的。只要股价上涨，空头就遭遇损失。事实上，即使再差的股票，也可能有一段上涨的空间，而在这个期间，许多空头就已经爆仓了。</p>
            <p> </p>
            <p style="font-weight:900">2.2 如何寻找做空标的</p>
            <p> </p>
            <p>对于一个公司，如果：财务数据表现很差、经常更换审计、处于过饱和的行业、P/E值远高于可解释的范围、遭遇失败的并购、面临潜在的公众形象问题、超过10%市值被卖出等行为，可以作为重点关注的对象。</p>
            <p>纯做空策略在市场上也有一些争议，首先他们非常不受上市公司的待见，尤其是被他们选作目标的那些公司。同时，这些做空者往往会使用一些手段来打压股价，存在扭曲和操纵市场的嫌疑。另一方面，这些空头也可以及时发现市场一些错误的定价，提高市场有效性。</p>
            <p> </p>
            <p style="font-weight:900">2.3 卖空交易</p>
            <p> </p>
            <p>卖空交易是对冲基金一项重要的工具，所谓卖空，就是卖出你本来没有的资产，这个资产通常可以通过抵押借来。看似很简单的一个操作，在金融市场中却一度引发争议，因为它是要靠市场下跌来赚钱的，因此受到更为严格的监管，尤其是在遇到金融危机的时候。</p>
            <p>交易机制</p>
            <p>首先对冲基金通过一些抵押物向证券公司借入S股票，然后将这些股票出售给卖家。</p>
            <p>然后在将来某一天，对冲基金从市场上买入等份额的S股票，换给证券公司，并支付一定的利息费用。</p>
            <p> </p>
            <p> </p>
            <p>以上介绍的这种情况，叫做有保护的卖空，因为在卖出的时候是持有股票的。与之相对应的情况，如果直接卖出不存在的股票，并且没有在交割日之前借来这些股票，叫做裸卖空。裸卖空可能导致市场上出现大量虚假的股票份额，导致价格被扭曲，这也是可能造成市场操纵的一个因素。但是有时候，例如有些股票短期内快速脱离基本面上涨的情况，一些做市商可能通过这种方式使股价回到合理区间。</p>
            <p>在这个过程中，证券公司借出股票，相当于把股票头寸变成了现金，而仍然保留了作为股票拥有者的利益。在卖空交易中，需要考虑两方面的头寸：作为股票的买家，拥有的是真实的头寸，而作为股票的出借人，拥有的是虚拟的头寸。对冲基金将替代证券公司来负责一些公司事务，如：公司分红，对冲基金也得支付等额股利给证券公司；公司拆股，对冲基金必须归还原来两倍数量的股份；公司配售一些股票，对冲基金必须把这些权利转移给证券公司，等。</p>
            <p>主要风险</p>
            <p>市场风险：卖空者必须买回这些股票归还，面临着市场上涨的风险。</p>
            <p>召回风险：卖出的股票被要求收回，卖空着又找不到合适的出借人，因此只好平仓，回购已经卖出的股票。</p>
            <p>流动性风险：市场缺少流动性，做空者买不到合适的股票</p>
            <p>决定因素</p>
            <p>决定卖空成本和难易程度的主要因素还是供需关系，大盘股由于被持有的很多，比较容易借到；而小盘股，尤其投资者认为被高估的小盘股，需求量很大，借入的成本很高。市场本身是一个平衡，但是如果卖空的人多了，这个市场的平衡就会被打破，因此大多数基金经理不愿意透露他们的空头头寸。</p>
            <p>潜在价值</p>
            <p>1.有助于促进市场的有效性，起到价格发现的作用，有研究表明，严格限制股票的借入，是导致互联网泡沫的重要原因。</p>
            <p>2.卖空有助于对抗金融欺诈和一些不公平的泡沫，有些高风险的因素人为操纵推高股价，也可以通过卖空来消除。</p>
            <p>3.提高经纪商的流动性，比如在持有某种股票的时候，仍然可以满足投资者买入的需求。</p>
            <p>4.可以用在一些套利策略（如统计套利、配对交易等）中，促进价格的合理性</p>
            <p> </p>
            <p>3. 市场中性策略</p>
            <p>之前说到的股票多空策略，并没有特别在意多头头寸和空头头寸的关系，因此根据基金经理对于市场的判断，多空策略经常会留下一些净敞口，最终的收益也会一定程度上和市场方向有关。市场中性策略的目的就是为了完全消除净敞口，多头和空头的头寸要随时调整，以确保投资组合的收益完全来源于股票选择，而与市场的方向无关。理论上来说，市场中性才是对冲基金的真正精华所在：就是创建一种投资组合，与市场涨跌无关，取得绝对收益。市场中性可以从几个不同的角度来分析：</p>
            <p> </p>
            <p style="font-weight:900">3.1 货币中性</p>
            <p>假设投资组合持有900美元多头头寸和600美元的空头头寸，为了保证美元中性，应该增加300美元的空头头寸。随着多头头寸市值增加，空头头寸市值减少，组合逐渐偏向净多头，因此需要动态调整整个组合的仓位。美元中性的方式很简单，值得注意的是，在调整仓位的过程中，还需要时刻留意那些未知的风险因素。</p>
            <p> </p>
            <p style="font-weight:900">3.2 Beta中性</p>
            <p>如果一个投资组合的收益和市场收益无关，那么我们可以称之为beta中性，这也意味着beta值等于0。根据现代投资组合理论，股票组合的风险可以由市场风险和特有风险构成，而市场风险就是由beta值来衡量的。</p>
            <p>投资组合的beta值是成分股的beta值的加权平均，因此仅仅保证货币中性，还不能确保整个组合的收益率和市场波动无关。例如，多头头寸的beta= 1.4，空头头寸的beta= 0.7，整个组合的beta= 50% x 1.4 + 50% x 0.7 = 0.35，这意味着美元中性投资组合的市场风险不等于0，和市场波动的相关性为正。</p>
            <p>为了保证beta中性，应该将空头头寸加倍，也就是美1美元的多头，对应2美元空头。有些读者可能疑问：为何投资经理要创建这样一个beta中性的投资组合？答案很简单：因为他只想承担他愿意承担的那部分风险，也就是选股技术的风险，而不是市场风险。选股能力才是区别不同基金绝对收益的来源，是真正的核心竞争力。</p>
            <p> </p>
            <p style="font-weight:900">3.3 行业中性</p>
            <p>Beta中性还不能完全保证不亏钱，例如，当多头头寸所在的行业突然下跌，而空头头寸所在的行业突然上涨，那两边都亏钱。同时，价格/成长、大小盘等风格因子变化，也可能造成类似亏损。为了避免这种情况的发生，需要在行业内、大小盘、或者价值/成长股之间，进行中性调整。</p>
            <p> </p>
            <p style="font-weight:900">3.4 因子中性</p>
            <p>以上的几种情况都可以看做是因子中性的一种特殊情况。多因子模型通常用来精确描述风险的来源，也可以表示资产收益对风险因子变化的敏感程度，因此我们也可以计算每个因子暴露值，然后将它中性化。如前文提到的市场中性，就是把市场暴露的因子beta中性化。Barra风险模型大致可以按照以下方式进行划分： </p>
            <p> </p>
            <p>这里的风险因子就包括了，市场风险、行业风险、宏观风险、微观风险等。理论上，可以将这些风险一一对冲，那么剩余的就是无风险收益率。因此事实上市场中性策略，只不过是在避免不想要的风险和减少所对应的收益之间的平衡。对于一个有经验的投资经理，往往可以非常精确地选择性地对冲自己不想要的风险，承担另一部分自己有把握的风险，以取得收益。</p>
            <p>例如：</p>
            <p>规模因子的暴露值是0.25：表明组合偏大盘，为了保证市场中性，卖空一些大盘股动量因子的暴露值是-0.14：表明组合偏向近期表现不好的股票，为了保证市场中性，卖空一些近期表现不好的股票，买进一些表现好的股票</p>
            <p>成长因子的暴露值是0.02：表明组合非常轻微地偏向成长股，为了保证市场中性，卖空一些成长股，买进一些价值股。</p>
            <p> </p>
            <p style="font-weight:900">3.5 常见策略</p>
            <p> </p>
            <p> </p>
            <p>配对交易</p>
            <p>这是最早出现的一类市场中性策略，最早可以追溯到1920年，由传奇交易员Jesse Livermore提出。他的交易策略简单可以总结成：找到那些涨跌幅度基本相同的股票；当出现价差偏移时，多时做空和做多；持有头寸直到价差恢复到正常水平。</p>
            <p>这种策略虽然简单，但是直到今天还在广泛地使用。它的主要观点就是相似特征的股票总是会同步涨跌，发现那些偶然偏移的机会来建仓，当价差恢复的时候取得收益。这在本质上也是一种均值回归策略，基于两种资产的相对关系。这种策略不一定能完全保持市场中性，实际过程中，会使用指数期货来调整头寸，来达到市场中性化。</p>
            <p>配对交易成功的关键在于发现有配对关系的一对股票，通过模型来预测价差在时间序列上的关系。根据基金经理的背景，主要有两类方式：第一类是通过基本面判断。通过选择最被低估的股票和最被高估的股票建仓，持有头寸直到价差充分回归。第二类是通过纯粹的统计学方法来发现这种配对关系。</p>
            <p>统计学方法通常可以覆盖市场上绝大多数的股票，并且不容易受到错误观点的影响，但在分析时间序列的关系时，也很难灵活地加入预先对市场的分析和判断。统计学模型通常采用“距离函数”来刻画两支股票之间的关系，其中最简单的是“跟踪方差”，这是标准化的价格序列之差的平方和。交易过程中，需要在价差突破某个临界值建仓，如偏离了历史平均值的2倍标准方差的时候，当价差回到正常水平的某个临界值时平仓。</p>
            <p>当然，还有更复杂的模型，比如股票之间协整关系，或者是通过连续时间随机过程，建立随机价差模型。这种方法可以比较方便地预测价格、计算预期持有期。此外，正交回归的方法也用于计算距离。</p>
            <p>另一方面，统计学的方法有可能造成数据的过拟合，也就是在训练样本上表现的很好，而在测试样本是表现不好，因此模型的假设非常重要。此外，基本面的改变也可能导致模型的失效。</p>
            <p>统计套利</p>
            <p>这是配对交易的延伸，差别在于它是对全体股票进行分析，按照一定的特征分成若干组，发掘组与组之间的平均偏差。因此，统计套利的本质还是去挖掘有效因子，包括价值因子、成长因子、量价因子、趋势因子等传统因子。一旦因子选定，就可以根据这个因子的大小将股票进行分组，然后分别做多和做空距离最远的两个组。策略的定义本身不难，但是其中涉及到的因子分析包括趋势/反转、成长/价值之间的风格轮动，却是最难把握的。</p>
            <p> </p>
            <p style="font-weight:900">4. 困境证券策略</p>
            <p style="font-weight:900">4.1 什么是困境证券</p>
            <p>有些对冲基金专注于金融市场黑暗一面的机会，比如那些遇到金融困境、违约甚至破产的公司的证券。这种基金有时被比喻为“秃鹫”，在市场中也是受到争议。许多人认为这是通过牺牲股东的利益，让陷入金融困境的投资人获利的一种手段。然而这种说法是片面的，困境基金更多的价值在于重组过程中扮演的角色。</p>
            <p>困境证券看似不那么吸引人：首先，机构投资者根据一些监管规定，是不可以购买投资级以下的证券；其次，困境证券流动性很差；再次，关于这类证券的信息太少。但对冲基金喜欢这样一个市场，因为市场对这样的证券缺乏足够的了解，无法认识到它的价值，利益就可以从这样的信息不对称中获取，这种机会包括了：</p>
            <p>1、销售压力带来的折价</p>
            <p>2、不论是重组还是停止运营，都可以通过出售部分业务来获得良好收益</p>
            <p>3、一些国家的监管允许困境证券的投资者进行资产剥离，并且将不良资产重新打包</p>
            <p> </p>
            <p style="font-weight:900">4.2 法律框架</p>
            <p>每个国家的破产法都不一样，以美国法律为例，它为困境中的公司提供了两种选择：一是通过削弱债务进行重组；二是破产、清算。在这里我们着重考虑选择一，这也是对冲基金主要关注的。</p>
            <p> </p>
            <p style="font-weight:900">4.3 估值</p>
            <p>首先需要区分经济困境和财务困境。经济困境表示公司在可持续经营的框架下，它的当前价值已经小于总资产价值，最好的办法就是破产和清算，它的价值就是资产出售的价格，介于市场价格和清算价格之间。财务困境表示现金流或者债务遇到了问题，最好的办法是出售一部分资产，或者重组，这是困境对冲基金的介入点。</p>
            <p>资产重组会产生新的资本结构，对于困境公司的估值是很难的，需要很好地估值理论、破产法和公司法律。估值的方法主要有：</p>
            <p>1、通过EBITDA和类似健康公司的价格乘数来计算公司价值</p>
            <p>2、通过类似公司近期的收购和支付情况来计算公司价值</p>
            <p>3、通过DCF和WACC现金流折现的方法来计算公司价值</p>
            <p> </p>
            <p style="font-weight:900">4.4 主动和被动</p>
            <p>主动管理的困境证券基金一般会通过购买大量的投票权或者可转债来深度参与目标公司的重组过程。而被动管理的基金一般是等待合适的机会（比如价格低于他们估计的公允价格时）买入，并且不会参与公司重组，只是等待价格上涨的时候卖出。两者面对的监管也是不同的，被动管理基金是在公开市场进行交易，而主动管理基金更多是通过非公开的场所进行交易。</p>
            <p> </p>
            <p style="font-weight:900">4.5 风险</p>
            <p> </p>
            <p>根据每个公司的不同情况，风险来源也会有所差异，主要包括：</p>
            <p>1. 财务风险：时间成本很重要，如果困境公司在很长的时间内还不能恢复，这会造成很大的风险。</p>
            <p>2. 偏多头：困境证券很难借到，一个很小的事件也可能造成股价很大的上涨，空头是很危险的，最终导致的结果是整个组合偏多头。</p>
            <p>3. 清算风险：重组失败，导致直接清算，这个价值是低于重组的。</p>
            <p>4. 内幕交易：那些不公开交易的证券容易造成内幕交易。</p>
            <p>5. 税务风险：重组过程中的运营净损失和债务收入的取消带来的税务问题。</p>
            <p>6. 流动性风险：买入困境证券经常需要持有若干年，并且严格限制赎回。</p>
            <p> </p>
            <p  style="font-weight:900">5. 并购套利策略</p>
            <p style="font-weight:900">5.1 历史回望</p>
            <p>并购套利，又叫做风险套利，是最早的事件驱动策略之一，它是从公司的并购或重组等活动中获得收益。投资的流程也相对简单：当目标公司发布并购重组的信息之后，套利基金买入这些公司的股票，赚取从信息发布之后价格与交易价格的差价。历史上比较著名的有Daniel Och的Och Ziff，Richard Perry的Perry Partners，以及Thomas Steyer的Farallon Capital。</p>
            <p>历史上有几次大的并购套利浪潮，每次都非常巧合地发生在股价和市盈率上升的阶段。这几个阶段分别是1895年到1903年的垄断并购；1920年到1929年的寡头并购；1955年到1973年的牛市并购；1974年到1989年的收购浪潮；以及1993年至今，也是历史上最广泛的并购浪潮。</p>
            <p> </p>
            <p> </p>
            <p style="font-weight:900">5.2 策略流程</p>
            <p>并购套利策略的执行相对简单，往往是从并购的消息发布开始。并购者以高于市场的价格对目标公司的股东发出了购买邀请，其收益来源主要是要约收购价与被收购公司股票市价之间的差额，称为收购套利溢价。</p>
            <p>如果并购成功，市场价格会逐渐收敛到收购要约价格；而如果并购失败，市场价格可能暴跌。因此，套利者需要仔细分析并购成功的概率和相应的风险，在合适风险收益比的情况下建立仓位。</p>
            <p> </p>
            <p style="font-weight:900">5.3 现金收购</p>
            <p>顾名思义，就是直接用现金购买目标公司的股票。一般来说这个要约价格对市场价格都有不同程度的溢价，若是在信息发布之前的30到90天，溢价大概在40%，若在信息发布之后，溢价大概在5%到15%。在这个交易中，并购套利者持有多头头寸，因此面临着市场风险，若是在这个期间市场下跌，他们也可能遭遇损失。一个解决办法是做空股指期货，当然，这只能起到部分保护作用，而不能完全对冲风险。</p>
            <p> </p>
            <p style="font-weight:900">5.4 换股收购</p>
            <p>这是通过收购方用自己的股票换取目标公司的股份来达成交易的。在这种情况下，要约收购价格就不固定了，而是根据收购者股票价格而定。在换股交易中，两个公司的股票价差预期收窄，收购者的股价下跌，目标公司的股价上涨，因此为了对冲风险，可以适当地做空价差。值得注意的是，这很难做到市场中性，因为做空并购者的股票的过程中可能导致它的股价大幅下跌，尤其如果这个公司规模不大的情况下。</p>
            <p> </p>
            <p style="font-weight:900">5.5 并购套利风险</p>
            <p>这种策略最核心的问题就在于并购成功与否，主要风险包括交易风险和时间风险。具体来说，包含了收购者和目标公司的态度、交易类型、溢价、目标公司的股权结构、套利者数量、反垄断竞争条款、经济形势等因素。</p>
            <p>并购套利并不是简单地买入持有，事实上在头寸建立到交易结束的这段时间里，需要时刻监视市场的变化，新的政策、消息都有可能导致价格的波动，必要的时候还需要调整仓位，以确保最好的结果。</p>
            <p> </p>
            <p style="font-weight:900">6. 可转债套利策略</p>
            <p style="font-weight:900">6.1 简介</p>
            <p>可转债是一种类似于债券，但是在一定的情况下可以转化为股票的证券，它经常是低于其公允价值发行的，这也意味着存在套利机会。最早的策略就是低价买入可转债，然后卖空发债公司的股票以对冲风险。经过不断发展，如今的可转债套利还包括了在信用风险、波动性、收益率曲线的凸性等方面的单向下注。可转债是一种相对比较复杂的证券，涵盖了股票、债券和期权的特性。</p>
            <p>运用这样一种工具进行套利，首先得有一个合理准确的估值。理论上说，这个价值应该是在一个深度的、流动性好的、有效的市场中的无套利价格。显然，这不是一件容易的事，主要源于以下几种风险：利率风险、信用风险、股票风险以及各种风险之间的相互作用。此外，可转债经常具有某些特殊的条款，如认购或者认沽条款，这些都很难准确定价。下面来看看学术界和工业界都是如何估值。</p>
            <p> </p>
            <p style="font-weight:900">6.2 学术估值</p>
            <p>学术上，关于可转债估值的研究是一个很有挑战并且也有很多研究成果的领域，主要包括三类方法：结构化方法、简化方法和基于模拟的方法。</p>
            <p>结构化方法是莫顿在1974年提出的，他设计了一个简单的模型将发债公司的信用风险和资本结构联系在一起。他注意到一个公司的资产价值等于负债加权益，它违约的前提是资产价值低于负债价值。基于这个方法，权益和负债都可以看作是总资产的或有求偿权，它们的价格可以通过B-S模型进行定价。后来的学者在这一模型里面陆续加入了期权、离散的红利、随机变化的利率等因素，最终确立了可转债的偏微分方程数值解。</p>
            <p>结构化方法对公司的信用风险具有较好的经济学解释性，它假设了公司自有一方面的债务。但现实中的公司债务都是多种多样的，这造成了建模的困难。同时，对于模型中的核心变量-资产的价值，无法直接获取到，这也进一步增大了模型实现的困难。</p>
            <p>第二种方法是简化的方法，它假设了公司的违约是外因造成的，源于某次突然的损失使得股价下跌至零。下一阶段违约的概率是基于一种特别的风险率，这是潜在状态变量的函数。后来，一些信用模型被用来刻画历史违约率和恢复率，或者价差的时间序列，这些模型最终就被用来处理可转债的定价问题。</p>
            <p>第三种方法是一种更为实用的，基于蒙特卡洛模拟的方法。这是将利率和股票价格根据统计学的方法进行成千上万次的模拟，每次模拟都可以得到一个可能的价格，通过取平均值得出估计的价格。</p>
            <p>以上几种估值方法都在理论上非常严密，但是实践过程中过于复杂，而且对输入参数特别敏感，始终没有找到一个很合适的定价方法。</p>
            <p> </p>
