            <p>发展简史</p>
            <p>上世纪90年代初，珠信基金的成立标志着我国投资基金（封闭式基金雏形）的起步。之后，天骥、蓝天、淄博等投资基金作为首批基金在深圳、上海证券交易所上市，标志着我国全国性投资基金市场的诞生。从封闭式基金十多年的发展历程看，封闭式基金大致经历了起步、规范和发展的几个阶段。</p>
            <p>1.起步期</p>
            <p>1991年7月，发行规模达6930万元的珠信基金（原名一号珠信物托）成立了，它是国内发行时间最早的基金。之后，武汉基金（第一期）、南山基金相继发起设立。</p>
            <p>（以上发展历史参见《封闭式基金-历史与现状》一文）</p>
            <p>1992年8月，金华市信托投资公司创立的“金信基金”在浙江金华市信托投资公司证券部按股票交易模式上市竞价交易。同时，深圳投资基金管理公司成立，这是我国大陆投资基金业中成立的第一家专业性基金管理公司。</p>
            <p>11月中国人民银行深圳经济特区分行批准深圳投资基金管理公司发行天骥基金，规模为5.81亿元，天骥基金是当时全国基金中发行规模最大的基金。此后又有多家基金发行，包括淄博基金等。</p>
            <p>1992年6月，中国人民银行深圳经济特区分行颁布了《深圳市投资信托基金管理暂行规定》，这是当时唯一一部有关投资基金监管的地方性法规。</p>
            <p>1993年3月，中国人民银行深圳经济特区分行批准天骥投资基金、蓝天基金作为首批基金在深圳证券交易所上市。</p>
            <p>同年8月20日，中国人民银行批准淄博基金在上海证券交易所上市，标志着我国全国性投资基金市场的诞生。此后，上海证券交易所和深圳证券交易所又陆续上市了几家基金，并与其他证券交易中心的基金市场进行联网交易，全国性基金交易市场已经初步形成。</p>
            <p>据统计，1993年1月至5月期间，中国人民银行广东、深圳、四川等分行共批准设立基金12家，规模达到18亿元人民币，国内基金业进入了快速扩张阶段。</p>
            <p>1992至1993年是投资基金成立较多的几年，至1993年底，共设立各类基金近50只，发行地主要集中在广东、黑龙江、深圳、沈阳、大连、海南、江苏等省市。其中基金发行规模最大的是深圳市，达13.7亿元人民币。但过快的扩张速度为基金的统一监管带来了较大的困难。</p>
            <p>1993年5月19日，中国人民银行作出了制止不规范发行投资基金的规定，其中投资基金的发行和上市、投资基金管理公司的设立以及中国金融机构在境外设立投资基金和投资基金管理公司，一律须由中国人民银行总行批准，任何部门不得越权审批。</p>
            <p>此后，除1993年9月经中国人民银行总行批准上海发行了额度各为1亿元人民币的金龙基金、宝鼎基金、建业基金外，相当长的时间里（直至1998年上半年），未再批准设立过各类基金，国内基金的发行陷入停滞状态。</p>
            <p>1996年3月18日，深圳基金指数开始编制，基准指数为1000点，将当时在深圳证券交易所直接交易或联网交易的10只基金全部纳入计算范围。</p>
            <p>据统计，截止于1998年初，我国共设立各类投资基金78只，募集资金总规模为76亿人民币，在上海深圳挂牌（联网）交易的基金共27只。在此期间，专业性基金管理公司很少（不足10家），基金总的说来规模很小，运作也很不规范，也称为“老基金”时期。</p>
            <p>2.规范和发展期</p>
            <p>1997年11月14日，《证券投资基金管理暂行办法》正式颁布。同时，由中国证监会替代中国人民银行作为基金管理的主管机关。从此，中国证券投资基金业进入了规范发展的崭新阶段。</p>
            <p>从1998年3月起，陆续有开元证券投资基金、金泰证券投资基金等多家大型基金上市，规模远远大于1992、1993年成立的基金，多为10亿至30亿人民币。</p>
            <p>1999年是基金业大发展的一年，基金迅速增加22只，资产规模跃升为484.2亿元；同时，这些基金在规范性方面也有比较大的进步，根据《证券投资基金管理暂行办法》的规定，基金投资范围仅限于国债和国内依法公开发行、上市的股票，基金的投资组合为80%投资于股票，20%投资于国债。在基金的发起、募集以及后期的运作上也有相对比较严格和细致的规定，如持有一家上市公司的股票，不得超过基金资产净值的10%；基金与基金管理人管理的其他基金持有一家公司发行的证券总和，不得超过该证券的10%；禁止基金之间相互投资；禁止将基金资产用于抵押、担保、基金拆借或者贷款；禁止基金从事证券信用交易等等。</p>
            <p>1999年3月，中国证券监督管理委员会发出了对原有投资基金进行清理规范方案的通知，各证券交易中心交易的基金逐步摘牌，交易所上市基金也进行清理规范。在经过一系列的基金合并、资产重组，将原先老基金的不良资产全部置换为流通性较强的上市公司股票、国债或现金资产，并在此基础上完成了基金的扩募和续期，最终实现了新老基金的历史过渡。如现在的“景博证券投资基金”即是由湘建信基金和湘农信基金合并而成；“同智证券投资基金”则为海湾基金、武汉基金、赣中基金、中盛基金、开信基金和长江基金重组而来。</p>
            <p>截至2002年9月，我国共有基金管理公司18家，已经批准筹备、正等待开业的基金管理公司有1家，另有14家（不包括中外合资基金管理公司）处于申请筹备阶段；国内已募集成立并已挂牌上市的封闭式基金达54只，筹资总额达807亿元。</p>
            <p>自从2002年下半年以来，封闭式基金的形势急转直下。2002年9月后至今，没有一只封闭式基金在投资基金市场发行。封闭式基金此次遭遇的“停滞”现象不同于1998年前的“停滞”。那时的“停滞”是出于整顿规范的需要（是主动的），而如今的“ 停滞”却发生于基金业大变革、大发展的年代（是被动的），个中缘由值得深思。</p>
