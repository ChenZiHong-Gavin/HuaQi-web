          <h1 style="color:#F00">基金定投是什么？</h1>
          <p>定投与银行的定存有点相像，定存也就是定期储蓄，定期向银行存钱。基金定投是指定期投入固定的金额投入与基金市场。定投与定存的区别在于定存的目的是储蓄，并不会太在乎收益，但基金定投更在乎收益而已，所以定投的首要目标是获得收益，其次才是存钱。</p>
          <p>投资两大因素，一是投资标的选择，二是交易时间点选择。通过深入的学习与实践，投资标的选择是相对容易的。然而交易时机的选择，相对很困难，其涨跌是由各种各样的因素组成，更谈不上所谓的规律及套路。</p>
          <p style="font-weight:900">基金定投本质就是放弃交易时间的选择，重抓投资标的选择上，通过长期的持有来获得收益。</p>
          <h1 style="font-weight:900">如何选择呢？</h1>
          <p>1、权益类与非权益类的选择。权益类指的就是包含股权的，如股票型、混合型、指数型。非权益类有债券型、货币型等。但定投选择非权益类是毫无意义的，非权益类收益较低、波动小，任何时间买进收益差异不大，跟定存又有何区别呢？  所以定投一般选择权益类的基金。</p>
          <p>2、被动型与主动型的选择。被动型一般指指数基金，所谓指数其实就是选取了不同市场中具有代表性的若干只股票，通过算法编织成一个指数，来代表某个市场的表现。指数基金的产品与比例是相对固定的，基金经理在选择与配置上基本上没有什么主动权，他干的活无非就是看看指数里有什么基金，什么比例，然后照着指数模板抄一遍即可，如果指数有变化跟着调整就交作业了，所以叫做被动型。那主动型就更容易理解了，基金经理的占主导位置，根据自身的能力及经验进行他认为靠谱的选择与配比。</p>
          <h1 style="color:#F00">投资策略建议</h1>
          <p>如果无法投入太多的时间与精力时，推荐被动型。沪深300指数、上证30指数、恒生指数都是具有代表性的，可以多多参考。</p>
