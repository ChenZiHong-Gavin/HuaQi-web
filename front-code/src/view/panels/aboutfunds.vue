          <h1>一、什么是基金</h1>
          <h2>1.1.股票vs债券vs基金</h2>
          <p>股票：代表一种股权（拥有权），和企业共进退。</p>
          <p>债券：代表一种债权，利息不收经营影响（借钱给别人，约定收益）</p>
          <p>基金：代表一种委托关系，委托专业机构帮你理财，一般而言，收益：股票>基金>债券，风险相反。</p>
          <h2>1.2.基金涉及到的主体（投资者、基金管理公司、基金托管公司）</h2>
          <img src="../../assets/images/image91.jpg">
          <p>（1）基金管理公司，就是负责管理基金的公司，他们的工作包括发起设立基金，进行基金运作，帮助投资者获得收益。</p>
          <p>（2）投资者，一个投资者可以同时投资于多个（没有限制，只要你钱多就行）基金。</p>
          <p>（3）基金托管公司，所谓“托管”，我们可以理解为托付保管，所以基金托管公司呢，其实就是被托付来保管基金资产的，他们的职责主要就是保管基金的资产，监督基金管理公司以及向监管机构出报告等等。</p>
          <h2>1.3. 基金的生命周期</h2>
          <p>产品设计——证监会审批——确认发行——公开发行（募集资金）——建仓期封闭——正式运作——清盘</p>
          <h1>二、基金的分类</h1>
          <p>界定范围：公募基金VS私募基金，所谓公和私，其实指的就是对公和对私，对公即对所有人都可以投资，一般买入门槛很低；对私，即对私人发行，一般门槛比较高，几十万上百万起。</p>
          <h2>2.1.最基础的分类——按投资标的分类</h2>
          <p style="font-weight:900">•货币型基金 </p>
          <p>o余额宝、招商招钱宝的本质就是货币基金，收益较低（但比银行活期高得多，目前在一年3%收益率左右徘徊），风险也很低，而且很灵活。</p>
          <p style="font-weight:900">•股票型基金</p>
          <p>o即大部分资金（不能低于80%的仓位）投资于股票的基金，一般按照主题分（行业，政策等）因其投资标的是股票，因此收益和风险在各类基金中都是最高的。</p>
          <p style="font-weight:900">•债券型基金</p>
          <p>o即大部分资金（80%以上）投资于债券的基金，大部分债券型基金都会标明投资债券的品种，如信用债、可转债等等。</p>
          <p style="font-weight:900">•混合型基金</p>
          <p>o可以投资于货币工具、股票和债券等等，一个什么都可以投的基金。</p>
          <p style="font-weight:900">•指数型基金</p>
          <p>o我们常见的指数有沪深300指数，上证50指数，深圳50指数等等，其实就是选取了不同市场中具有代表性的n只股票，通过一些算法把它们编织成一个指数，来代表某个市场的表现。所以投资于指数的基金，本质上就是投资于这些指数所包含的股票的基金。</p>
          <h2>2.2. ETF、ETF联结、LOF、QDII 、分级基金、保本基金、量化基金</h2>
          <p>（1）场外交易和场内交易：所谓“场”指的就是二级市场，也就是证券交易所。所谓场内交易，就是指在证券交易所里可以交易的；场外交易，就是不能在证券交易所里交易的。</p>
          <p>（2）开放式基金和封闭式基金：封闭式基金，即买了之后，是不能赎回的，只能够转让给其他人，即只能场内交易；开放式基金，买了之后是可以随时赎回的，但是不能够转让给其他人，即只能场外交易。</p>
          <p>综上，一般基金不是封闭式就是开放式，</p>
          <p>所谓封闭式，只能够交易，不能够申赎；</p>
          <p> 所谓开放式，不能够交易，只能够申赎。</p>
          <p style="font-weight:900">•ETF</p>
          <p>o学名交易型开放式指数基金（俗称交易所交易基金），所谓交易型开放式，即它是一个开放式基金，又可以在二级市场交易，同时是指数基金。（牛逼在可以场内交易，交易费用又比指数基金低）</p>
          <p style="font-weight:900">•LOF</p>
          <p>o学名交易型开放式基金，区别于ETF：a. 不是指数基金，投资标的面更广;b.申赎门槛比ETF（几十万）低。（牛逼在比ETF灵活）</p>
          <p style="font-weight:900">•ETF联接</p>
          <p>o基金公司专门为了让那些没有证券账户，无法在证券公司进行该ETF交易的投资者也能对该ETF进行投资，而复制了一个场外可以申赎的指数基金。</p>
          <p style="font-weight:900">•QDII</p>
          <p>o是指在一国境内设立，经该国有关部门批准从事境外证券市场的股票、债券等有价证券业务的证券投资基金。</p>
          <p style="font-weight:900">•分级基金</p>
          <p>o所谓分级基金，就是把一个母基金劈成两半，一半是优先级，也称A类份额，另一半是落后级，不对，是进取级，也称B类份额。A份额和B份额的资产作为一个整体投资，其中持有B份额的人每年向A份额的持有人支付约定利息，至于支付利息后的总投资亏损由B份额持有人承担。（风险高，B资金门槛高，不适合小白）</p>
          <p style="font-weight:900">•保本基金</p>
          <p>o保本基金就是在一定期间内，对所投资的本金提供一定比例的保证保本基金，基金利用利息或是极小比例的资产从事高风险投资，而将大部份的资产从事固定收益投资，使得基金投资的市场不论如何下跌时，绝对不会低于其所担保的价格，而达到所谓的「保本」作用。</p>
          <p style="font-weight:900">•量化基金</p>
          <p>o就是“通过借助统计学、数学方法，运用计算机从海量历史数据中寻找能够带来超额收益的多种“大概率”策略，并纪律严明地按照这些策略所构建的数量化模型来指导投资，力求取得稳定的、可持续的、高于平均的超额回报，其本质是定性投资的数量化实践。从投资标的分类来看，量化基金为大多为混合基金和股票基金</p>
          <h1>三、基金的基本要素</h1>
          <h2>3.1. 业绩和净值，主要展示基金的收益表现</h2>
          <p>•业绩表现</p>
          <p>o含义是基金历史收益情况（绝对收益表现+相对收益表现），通常而言都会用折线图来直观展示，大部分基金的收益曲线，都会拿来和“沪深300”作对比。</p>
          <p style="font-weight:900">•净值</p>
          <p>o所有基金的默认初始单位净值都是1，之后随着业绩表现净值也随之变化，假设上涨了20%，那么净值就从1涨到了1.2。</p>
          <p>o累计净值是单位净值加上历史的所有分红。通常而言，基金的累计净值就是单位净值，除非该基金进行过分红或者拆分。</p>
          <p>o货币基金净值一直为1，所以看万份收益和七日年化</p>
          <h2>3.2. 产品档案，包括基金经理、基金持仓等内容</h2>
          <p>产品档案即基金产品的基本信息，包括基金概况、基金经理、基金持仓等等。</p>
          <p style="font-weight:900">•基金经理</p>
          <p>o1.任职时间长，经得起时间的考验</p>
          <p>o2.长期表现优异，平均年化收益高</p>
          <h2>3.3. 交易规则，包括交易确认时间，费率等内容。</h2>
          <p>•T日，即”Trading Day“交易日的意思，以每天的15:00（同股市收盘时间）为分界线，过了15:00即为下一个交易日。</p>
          <p>•一般而言，申购基金都是T+1个日确认，而赎回基金，则有的是T+1日钱到账，有的是T+2日到账，具体时间跟平台和银行都有关系。</p>
          <p style="font-weight:900">•合适的基金</p>
          <p>o主动型基金是由基金经理根据自己的投资理念进行管理的，具备主管能动性，这类基金如果长期年化收益高，表现稳定，可以称之为“好”。</p>
          <p>可按照一些既定标准去选择，比方说成立时间较长，平均年化收益高，回撤较小等。或通过选择基金经理的方式来反选基金。</p>
          <p>•被动型基金，其投资是完全复制某一指数的成份，基金经理只需被动按方抓药，因此无所谓好与不好，更多判断的是当下投资是否合适。</p>
          <p>沪深300指数：代表的是中国A股，即如果A股大盘长期向好，这个指数也会同步增长。</p>
          <p>上证50指数：代表的是A股中的大蓝筹股，也就是白马股、优质股，像什么中国平安啊，招商银行等优质公司。这些可是中国的核心资产，强烈推荐。</p>
          <p>恒生指数：代表港股大盘，港股过去一年表现强势，但估值仍然处于中位，接下来应该还会有所表现。</p>
          <p style="font-weight:900">•合适的时机</p>
          <p>•均值回归原理 </p>
          <p>基金的投资完全可以忽略小的波动，而着眼于大的周期。即在市场整体估值偏低的时候持续买入，而在估值偏高的时候陆续卖出即可。</p>
          <p>•市盈率=股价/每股收益，是最常用的估值指标之一 </p>
          <p>横向比较，就是比较行业，看下这个公司的市盈率相对于行业的平均市盈率是高是低。</p>
          <p>纵向比较，查下下这个公司过往的市盈率，看下目前的市盈率是处在什么样的一个位置。</p>
