      <p>加密货币基金是一种新型投资工具，与对冲基金等传统组合投资类似，但完全由数字资产组成。因此，他们的规则与传统规则相比略有不同。对于想进入这个有趣的新领域的人来说，了解它们之间的差异以及在哪里参与是关键，因此，我们在篇实用指南中概述了要点。</p>
      <p>什么是加密货币基金?</p>
      <p>术语“加密货币基金”是指包含各种不同数字资产的投资组合，通常由一个或几个人管理。然后，投资者可以购买这些基金，以便他们可以随着基金价值的增长而分享利润。根据Crypto Fund Research的数据，其中一半以上的基金是风险投资基金，其余主要是对冲基金。</p>
      <p>风险投资基金包括各种各样的投资者，他们汇集资金以购买具有高增长潜力的小型企业。当然，在加密货币基金中，这些企业是新项目和山寨币。一旦资产得到足够的增长，它们通常会被出售，而投资者则会从中获利。</p>
      <p>对冲基金是一种积极管理的投资组合，旨在将市场风险降到最低，因此被称为“对冲”。对冲基金可以由任何资产组成，但是多头和空头策略通常都使用不同的资产，使投资组合多样化，以使基金能够抵抗高波动性，甚至在高波动性期间获利。同样，这些基金通常由小团队管理，并且通常只对高端投资者开放，最低投资额在几万至几十万美元之间。</p>
      <p>传统的对冲基金通常还具有最短的时间限制，因此，例如，投资者必须保证他们的钱至少存在基金里一年。他们还倾向于收取相当高的费用，约占利润的20%，以激励经理做出稳定的业绩。关于这一点，这需要信任管理战略的团队，并且不能保证基金最终会获得回报。如果管理不善，这些基金本应防范的市场波动也可以迅速消除。今年3月，随着冠状病毒市场的恐慌，加密资产价格大幅下跌。一些加密货币基金没有做好准备应对如此突然的下跌，因此崩溃了。</p>
      <p>加密货币基金经理使用的常见策略</p>
      <p>在这一点上，我们应该探索基金经理用来增加投资的策略。一种常见的策略被称为“股票多空”策略。在这种情况下，基金经理会关注他们认为被低估和高估的资产，然后相应地建立多头和空头头寸。如果他们的分析是正确的，那么无论市场是上涨还是下跌，他们的投资组合都应该会获利。</p>
      <p>一种类似的策略被称为“市场中立”。在这里，目标是使多头和空头头寸保持平衡，以使市场风险净敞口为零。因此，经理可能在同一行业或资产中持有50%多头和50%空头头寸，以期减少波动带来的风险。应当指出，降低风险通常也意味着较低的回报，对于某些人来说，这是可以接受的折衷方案。</p>
      <p>另一种常用的策略是套利。套利有很多种，但是通常的想法是在一个交易所购买资产，然后在价格更高的另一家交易所出售。这在传统的对冲基金中很常见，但是由于加密货币市场年轻且不稳定的性质，它通常提供更有利可图的机会。不同平台对各种资产提供略有不同的价格是很常见的，如果可以动作足够快，那么获利就相对容易。话虽如此，速度是关键，这使得该策略成为高频交易者的普遍喜爱。</p>
      <p>还有其他一些策略，比如“全球宏观”策略，它着重于根据市场内部更大的趋势建立头寸，以及“只做空”策略，它基本上专注于明确做空那些基金经理认为估值过高的资产。最后，有“定量”策略，它仅专注于模型、数据和研究以构建投资组合。实际上，使用多种不同的策略并不少见，但至关重要的是，基金经理必须了解他们实施的任何一种策略，并向投资者保持透明。</p>
      <p>多种不同的投资方式</p>
      <p>通常，投资加密货币基金所涉及的最大风险就是：客户需要信任其基金背后的人，这就是进行研究很重要的原因。经理们愿意分享更多的有关他们是谁，如何管理以及他们的过往记录的信息，就可以帮助确定他们是否适合投资者。这就是为什么，对许多人来说，与一家信誉良好的公司合作是他们将得到投资回报的重要组成部分。一些知名的加密货币基金包括Digital Currency Group, Galaxy Digital 和 Pantera Capital等。他们都特别关注加密货币和其他数字资产。</p>
      <p>当然，这些通常仍需要合格人员进行大量的前期投资。然而，想要参与这类投资的散户投资者可能会考虑像Tokenbox这样的项目。除了作为一个普通的钱包和交易所，Tokenbox允许用户“通证化”他们的投资组合，以及投资于其他人的投资组合的代币。这是一种简化方式，外汇返佣www.fx61.com/既可以推出新的加密货币基金，也可以参与现有的加密货币基金。与获胜的投资组合挂钩的代币本身可以买卖，它们的价值明显与基金的表现挂钩。然后，经理们可以展示他们的成功，试图吸引更多的支持者。所有这些都不需要大规模的初始投资就可以实现，而更像是在交易所购买任何一种加密货币。</p>
      <p>这个市场可以期待什么?</p>
      <p>这些天，加密货币基金的前景相当光明。普华永道(PricewaterhouseCoopers)和埃尔伍德资产管理服务有限公司(Elwood Asset Management Services Ltd.)的一份报告显示，这些基金管理的资产总值从2018年的10亿美元增长到2019年的20亿美元，仅一年市场规模就翻了一番。</p>
      <p>最重要的是，2019年这些投资的中位数回报率为30%，较2018年略有下降，但仍远高于大多数传统对冲基金。当然，这是由于市场上有上升空间。尽管可以找到各种各样的加密货币产品，但研究发现，有97%基金配置了比特币(BTC)，其次是67%基金配置了以太坊(ETH)，以及三分之一基金配置了XRP(XRP)，比特币现金(BCH)和Litecoin (LTC)。</p>
      <p>这一切都表明，这是一个真正刚刚开始探索的市场。随着加密货币的大规模采用，这些投资工具的数量和价值将继续上升是合乎逻辑的。风险总是存在的，这就是为什么投资者必须始终做好功课，但数字资产尚未开发的潜力看起来很有前景。如果更多的散户投资者也能进入这个领域，那么加密货币基金的故事可能才刚刚开始。</p>
