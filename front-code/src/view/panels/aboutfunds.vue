            <h1>一、新手购买基金的买入策略</h1>
            <p>简单来讲，基金的买入可以是一次性买入，也可以是基金定投，个人认为前者主要适合基金业绩稳定，稳步上涨，或者是行情较好的情况下，这时候可以一次性在较低点买入，获取较高收益;而后者则主要适合波动较高的基金，或者是无法把握行情的时候，因为大多数人很难判断行情的高低点，所以采用定投的方法可以避免买在高点的尴尬，在高点买入较少份额，在低点买入较低份额，从而实现成本摊低。</p>
            <h1>二、购买基金的时候应该注意什么</h1>
            <h2>1、买新不买旧</h2>
            <p>对于好多基金新手而言，总喜欢购买新基金，认为新基金费率相对较低，可以降低成本，并且新基金具有发展潜力，当时我也是这么认为的，所以购买了一些新的基金，一年多来虽不说赔，但也没赚。新基金没有过往业绩作参考，无法判断基金走势，也无法获取基金经理的投资策略，并且多数新基金由新的基金经理掌控，对于基金经理的投资能力无从判断，基金经理的投资策略也没有经过市场的检验。</p>
            <h2>2、买低不买高</h2>
            <p>买低不买高可以说是大多数基金新人都会走进的误区，往往认为基金净值低，买入成本低，并且基金净值已经很低了，不会再继续走低了，大有可能触底反弹，获取较大收益，殊不知多少人去抄底，却抄在了半山腰!而对于基金净值高的基金，大多数新手都会被高净值所吓倒，基金净值高也是基金业绩持续走好的一种表现!</p>
            <h2>3、买短不买长</h2>
            <p>很多人买基金给买股票一样，以买股票的方式买基金，殊不知基金通常需要长期持有才会见收益，当然碰到特殊情况也要学会止损。当然，大多数情况而言，还是长线持有，买基金频繁进行短线操作，这样不仅浪费交易手续费，没准还会错失大把大涨的行情。</p>
            <h1>三、基金的初步选择方法</h1>
            <h2>1、主题选“基”法</h2>
            <p>顾名思义，就是根据基金主题的划分，在自己看好的主题中选出自己想要投资的基金主题，如银行、证券、保险、食品饮料、煤炭、钢铁、新能源汽车等，当然这些主题不用我们自己去整理与总结，已经有专业的基金销售平台为我们做好了分类整理，我们只需要点进去选择基金就好了。</p>
            <h2>2、根据基金类型选“基”</h2>
            <p>我们都知道，根据基金投资的对象不同，可以将基金分为股票型、混合型、债券型、货币型等这么几个大类，每种基金类型对应的风险程度也不同，如股票型基金，六成以上投资的都是股票，所以其风险也是最大的，适合激进型投资人投资，所以，我们可以根据自己的风险偏好，选择自己想要投资的基金类型。</p>
            <h2>3、根据指数选“基”</h2>
            <p>众所周知，我国内地证券交易所主要是深证和上证，股票指数是判断市场走势的重要工具。</p>
            <p>我国内地股票指数主要有上证指数、深证指数、沪深300指数、中小板指、上证50指数、中证500指数、创业板指等，所以我们也可以根据这些指数选择基金，如上证50指数基金、上证综指指数基金、创业板指数基金等，一般来讲指数基金与指数的变化趋势是比较贴合的，当前总体上来看，我国股票指数水平还相对较低，发展前景很大，所以买这些指数基金，耐心等待，长期持有，收益还是可期的。</p>
            <h1>四、基金进一步删选策略</h1>
            <p>经过了基金的初选，我们大致可以确定要买哪种主题、哪种类型以及追踪哪种指数的基金了，然后我们就要进行细致的确定购买哪只金了，如何从众多基金中选出一只好的基金?</p>
            <p>一是要选基金公司，尽可能的选择大型基金公司，业绩好的基金公司，排名靠前的基金公司，这些信息可以从专业的基金销售网站上查询;</p>
            <p>二是要选择好的基金经理，基金经理是基金的掌舵人，基金经理的能力直接影响基金业绩，选择基金经理很重要，要看基金经理的专业能力，从业经验，要看基金经理掌管基金的过往业绩等;</p>
            <p>三是要看基金的历史业绩，特别是近一个月、三个月、六个月、一年、两年的业绩，从基金的历史业绩中大致可以判断出基金经理的投资策略、投资能力，从而对基金的未来成长有较好的把握，尽量选择历史业绩在同类基金中排名靠前1/4的基金，选择好基金以后就可以投资了。</p>
            <p>通过以上几个方面选好基金之后，还要看看这只基金是否适合自己，基金的投资目标、投资对象、风险水平是否与自己的目标相符。在基金投资上，没有最好，只有最合适的，新手投资者需谨记。新手购买基金的方法就介绍到这里，建议投资者在挑选基金产品时不要一味地追逐短期业绩排名，要将基金作为长期投资工具，选择长期业绩表现优异的基金以理性的心态进行投资。</p>
            <p>基金的分类：</p>
            <p>根据不同标准，可以将证券投资基金划分为不同的种类：</p>
            <p>（1）根据基金单位是否可增加或赎回，可分为开放式基金和封闭式基金。开放式基金不上市交易（这要看情况），通过银行、券商、基金公司申购和赎回，基金规模不固定；封闭式基金有固定的存续期，一般在证券交易场所上市交易，投资者通过二级市场买卖基金单位。</p>
            <p>（2）根据组织形态的不同，可分为公司型基金和契约型基金。基金通过发行基金股份成立投资基金公司的形式设立，通常称为公司型基金；由基金管理人、基金托管人和投资人三方通过基金契约设立，通常称为契约型基金。我国的证券投资基金均为契约型基金。</p>
