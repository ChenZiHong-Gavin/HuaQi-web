            <p>这是一个对于刚入门的投资者的好问题。讲之前，先推荐一本好书《Efficiently Inefficient》（作者：Lasse Heje Pedersen）。它对于想了解对冲基金的朋友，是一本很好的启蒙书籍。不说废话，讲正题。从对冲基金的角度，交易策略可以有以下分类（来自《Efficiently Inefficient》）：</p>
            <img src="../../assets/images/image101.jpg">
          <p>我们可以先把交易策略大体分成三类：1）股票策略 2）宏观策略 3）套利策略。其中，股票策略和宏观策略的收益主要来自投资目标的实际价值（absolute value）的变化，而套利策略的收益来自一对或一组投资目标的相对价值（relative value）的变化。这三者不是完全的独立，比如套利策略也有应用于股票市场，宏观资产配置也会借鉴股票策略中基本面分析方法。之所以这么分是因为三者有各自显著的特点。下面我们来逐一介绍：</p>
          <p>1）股票策略：股票策略主要指的是单一的应用于股票市场的交易策略。按照人的主观和计算机在策略的参与程度，我们把股票策略分成主动权益投资和主动量化投资。这里的主动投资更准确的翻译是决定型交易，之所以称为主动权益投资，是因为这是业界一般的称呼。主动权益投资主要是靠投资者的主观判断，他们通过对行业和企业的深入调查，形成自己的投资逻辑，然后进行筛选股票。这里又根据交易的限制分为多空策略，做多策略和做空策略。这里提一下做空策略（见于国外），只做空的投资者往往会把目标锁定在，那些对外披露的报告和实际表现不符的公司，然后去调查该公司的财务状况是否作假。该策略类型的代表就是浑水公司（Muddy Waters Research）。
相较于主动权益投资，主动量化投资是把自己的逻辑输入计算机，通过计算机的快速运算，来构建自己的投资组合。它和主动权益投资的区别体现在研究的深度和广度上。量化投资依赖于数据。换句话说，对于那些不是以数据形式存在的信息（比如与他人的谈话），计算机是没法获得的，也无法转化成交易信号。从这个角度来看，量化投资对单一股票的研究深度不如主动权益投资。但是，借助于计算机的快速处理能力，量化投资所构建的自动化模型，能在短时间内消化各种类型的数据信息，并且把它转换成有价值的交易信号。从这个角度来看，量化投资在研究的广度上比主动权益投资更具有优势。在中国的股票市场，目前主要存在的量化交易策略是多因子选股模型（具体不在这里做介绍）和一些基于流动性的高频交易策略。前者更适用于资金规模大的公募基金，后者则适用于追求短期高回报的私募基金。
          </p>
          <p>2）宏观策略：宏观策略的投资范围不局限于单一类型的市场，而是进行全类型市场的投资。这一类型的策略又可以分成以期货为投资工具的CTA策略，和宏观资产配置策略。CTA策略是动量策略的代表作。动量策略又称作趋势型策略。它研究的是价格的变化趋势，基于行为金融学，找到价格变化背后的规律（动量和反转），通过趋势变化的规律赚取收益。CTA通过期货标的物，可以把自己的投资范围扩大到各个类型的资产，这样的目的是为了分散动量策略本身的高风险。宏观资产配置策略多被一些全球资产管理公司以及投行（如德意志银行）采用。策略主要研究的是宏观经济的变化，然后做多或做空某一区域的所有类型的市场。比如如果某资产管理公司相信中国经济会持续高速增长，它就会做多中国的股指，做多中国的国债，做多与中国贸易有关联的大宗商品等等。</p>
          <p>3）套利策略：套利在这里是一种方式，因此它理论上可用于不同类型的市场。对于固收类产品，因为未来的现金流动比较固定，所以其价格与到期时间，利率，通胀，信用利差之间的关系更为确定。借助这个特点，投资者能更容易找到固收类产品之间的关系，也产生出更为多样的套利策略。另外，在海外市场，固收类产品有着更丰富的结构特性和相应的衍生品（如CDS）作为辅助，所以相应的策略比国内市场更为多样。事件驱动类的套利一般用在兼并收购这类事件，通过预测事件是否成功，从而做多或做空与参与者相关的股票，债券等产品。</p>
          <p>当然，策略的分类方式不是固定的。本篇只是借助《Efficiently Inefficient》的对冲基金策略的分类方式对几个常见的交易策略进行了介绍。细化到量化交易的策略，可以包括股票市场的多因子选股模型，高频交易，CTA，宏观资产配置，固定收益套利等。像高频交易，固定收益套利这类的策略，底下还有很多细分的量化交易策略。具体的，可以看看专门这方面的书籍。</p>
          </el-tab-pane>
