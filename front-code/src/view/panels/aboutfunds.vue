          <p>假设您有一笔钱想投资债券、股票啦这类证券进行增值，但自己又一无精力二无专业知识，三呢钱也不算多，就想到与其他１０个人合伙出资，雇一个投资高手（理论上比我还高点的），操作大家合出的资产进行投资增值。但这里面，如果10多个投资人都与投资高手随时交涉，那事还不乱套，于是就推举其中一个最懂行的牵头办这事。定期从大伙合出的资产中按一定比例提成给他，由他代为付给高手劳务费报酬，当然，他自己牵头出力张罗大大小小的事，包括挨家跑腿，有关风险的事向高手随时提醒着点，定期向大伙公布投资盈亏情况等等，不可白忙，提成中的钱也有他的劳务费。上面这些事就叫作合伙投资。</p>
    <p>将这种合伙投资的模式放大１００倍、１０００倍，就是基金。</p>
    <p>这种民间私下合伙投资的活动如果在出资人间建立了完备的契约合同，就是私募基金（在我国还未得到国家金融行业监管有关法规的认可）。</p>
    <p>如果这种合伙投资的活动经过国家证券行业管理部门（中国证券监督管理委员会）的审批，允许这项活动的牵头操作人向社会公开募集吸收投资者加入合伙出资，这就是发行公募基金，也就是大家现在常见的基金。</p>
    <p>基金管理公司是什么角色？基金管理公司就是这种合伙投资的牵头操作人，不过它是个公司法人，资格要经过中国证监会审批的。基金公司与其他基金投资者一样也是合伙出资人之一，另一方面由于它牵头操作，要从大家合伙出的资产中按一定的比例每年提取劳务费（称基金管理费），替投资者代雇代管理负责操盘的投资高手（就是基金经理），还有帮高手收集信息搞研究打下手的人，定期公布基金的资产和收益情况。当然基金公司这些活动是证监会批准的。</p>
    <p>为了大家合伙出的资产的安全，不被基金公司这个牵头操作人偷着挪用，中国证监会规定，基金的资产不能放在基金公司手里，基金公司和基金经理只管交易操作，不能碰钱，记帐管钱的事要找一个擅长此事又信用高的人负责，这个角色当然非银行莫属。于是这些出资（就是基金资产）就放在银行，而建成一个专门帐户，由银行管帐记帐，称为基金托管。当然银行的劳务费（称基金托管费）也得从大家合伙的资产中按比例抽一点按年支付。所以，基金资产相对来说只有因那些高手操作不好而被亏损的风险，基本没有被偷挪走的风险。从法律角度说，即使基金管理公司倒闭甚至托管银行出事了，向它们追债的人都无权碰大家基金专户的资产，因此基金资产的安全是很有保障的。</p>
    <p>如果这种公募基金在规定的一段时间内募集投资者结束后宣告成立（国家规定至少要达到1000个投资人和2亿元规模才能成立），就停止不再吸收其他的投资者了，并约定大伙谁也不能中途撤资退出，但以后到某年某月为止我们大家就算帐散伙分包袱，中途你想变现，只能自己找其他人卖出去，这就是封闭式基金。</p>
    <p>如果这种公募基金在宣告成立后，仍然欢迎其他投资者随时出资入伙，同时也允许大家随时部分或全部地撤出自己的资金和应得的收益，这就是开放式基金。</p>
    <p>不管是封闭式基金还是开放式基金，如果为了方便大家买卖转让，就找到交易所（证券市场）这个场所将基金挂牌出来，按市场价在投资者间自由交易，就是上市的基金。</p>
    <p>现在我们再来读下面的基金的概念，就不太晕头了吧。</p>
    <p>证券投资基金是一种利益共享、风险共担的投资于证券的集合投资理财方式，即通过发行基金单位，集中投资者的资金，由基金托管人托管（一般是信誉卓著的银行），由基金管理人（即基金管理公司）管理和运用资金，从事股票、债券等金融工具的投资。基金投资人享受证券投资的收益，也承担因投资亏损而产生的风险。我国基金暂时都是契约型基金，是一种信托投资方式。</p>
    <p>证券投资基金的特点：</p>
    <p>1、 专家理财是基金投资的重要特色。基金管理公司配备的投资专家,一般都具有深厚的投资分析理论功底和丰富的实践经验,以科学的方法研究股票、债券等金融产品，组合投资,规避风险。相应地,每年基金管理公司会从基金资产中提取管理费,用于支付公司的运营成本。另一方面,基金托管人也会从基金资产中提取托管费。此外,开放式基金持有人需要直接支付的有申购费、赎回费以及转换费。上市封闭式基金和上市开放式基金的持有人在进行基金单位买卖时要支付交易佣金。</p>
    <p>2、 组合投资，分散风险。证券投资基金通过汇集众多中小投资者的资金，形成雄厚的实力，可以同时分散投资于很多种股票，分散了对个股集中投资的风险。</p>
    <p>3、 方便投资，流动性强。证券投资基金最低投资量起点要求一般较低，可以满足小额投资者对于证券投资的需求，投资者可根据自身财力决定对基金的投资量。证券投资基金大多有较强的变现能力，使得投资者收回投资时非常便利。我国对百姓的基金投资收益还给予免税政策。</p>
    <p>附带解释下证券基金有关的部分问题。</p>
    <p>什么是基金的认购费和申购费？</p>
    <p>就是你投资入伙时要交的费用，因为基金公司宣传吸收投资人的活动是要费不少钱的，这些费用自然不能他一家出，另外通过提高你入伙的成本，降低你入伙不久就想离开的欲望。</p>
    <p>什么是基金的赎回费？</p>
    <p>就是你撤回投资和收益是要交的费用，原因与上面类似。还有一条，就是有人撤资走的时候，为了还给你现金，基金可能不得不卖掉一些债券股票，这是对基金资产不利的动作，对其他不撤资的合伙人的利益有不良影响，所以让你留下点费用作补偿。</p>
    <p>什么是基金的转换费？</p>
    <p>就是同一家基金公司操作着多个基金，你持有其中一个基金，想把这个基金按同等资产数量换成该基金公司操作的另一种基金，应向基金公司交付转换的费用，原因也与上两条同理，主要为了提高你变动的成本，不愿让你频繁变动。</p>
    <p>什么是基金交易佣金？</p>
    <p>就是上市的基金在交易所市场转让时，为你提供交易服务的证券公司营业部向你收的劳务费。</p>
    <p>证券投资基金为什么有那么多种？</p>
    <p>这是因为不同的基金自己规定的主要投资方向和投资对象有区别。</p>
    <p>股票型基金就是绝大部分资金都投在股票市场上的基金；</p>
    <p>债券型基金就是绝大部分资金都投在债券市场上的基金；</p>
    <p>混和型基金就是根据情况将部分资金投在股票上而另一部分资金投在债券上的基金（当然这种投资比例是可变化调整的），甚至根据事先的规定也可以部分投资在其他品种上；</p>
    <p>货币市场基金就是全部资产只投在货币市场上的各类短期证券（风险很低但收益也低）的基金。</p>
    <p>这些基金的投资风险由高到低的顺序大致是，股票型基金、混和型基金、债券型基金、货币市场基金。</p>
    <p>由于风险高低不同，所以投资者应根据自己对风险的承受能力来选择风险水平适合自己的基金投资入伙，也可以通过低风险基金、中等风险基金和高风险基金都投资一部分的办法来分散风险和平衡收益水平，这种行为就叫作投资组合。</p>
    <p>不同的基金在自己的名称上冠以什么“增长”、“价值”、“行业”、“蓝筹”、“小盘”、“周期”、“消费品”等等的字号，是将自己的主要投资策略风格标在名称上以便投资者一目了然，当然也不排除部分基金只是当初为了找个好名头切入点以便中国证监会容易批准成立。</p>
    <p>上面讲的主要是证券投资基金。基金还有专门投资于房地产的房地产基金，专门投资于期货期权的期货和期权基金，专门投资于黄金市场的黄金基金，专门投资于实业的产业基金。这些基金对我们初涉基金投资的人来说投资机会不多，先搞通上面几种最常见的证券基金后再说。</p>
    <p>基金净值的计算与分红</p>
    <p>基金净值的计算工作,每天由托管人(托管银行)在每日收市后计算并公布;开放式基金净值每天公布的是前一天的净值,封闭式基金每周公布一次,一般当周周日即可查到.</p>
    <p>再说说基金分红:基金分红是为了给投资者回报.对开放式基金还有降低基金净值,使后续申购者觉得便宜,继续申购;也有开放式基金把分红转为份额的,道理都基本一样.封闭式基金分红直接选择分现金,这样可把净值减下来,有助于减小折价率.不管是哪种基金,分红是事先在基金契约中约定好的,只有基金赚钱,才可能分红.一般基金都约定一年至少分红一次,而且当年可分收益的80%---90%要分红.如果基金不赚钱,或净值低于面值,基本无红可分.封闭式基金从2001年后少有分红,就是这个道理,大熊市,基金也难赚钱.如果逼它分红,那就像猪八戒啃猪手---只能自残骨肉了! </p>
    <p>6。基金与股票、债券有哪些区别？ </p>
    <p>反映的经济关系不同：股票反映的是一种所有权关系；债券反映的是债权债务关系；基金反映的是信托关系，是一种受益凭证，投资者购买基金份额就成为基金的受益人； </p>
    <p>所筹资金的投向不同：股票和债券是直接投资工具，主要投资实业领域；基金是一种间接投资工具，所筹集的资金主要投向有价证券等金融工具； </p>
    <p> 投资收益与风险大小不同：股票价格的波动性大，是一种高风险、高收益的投资品种；债券可以给投资者带来一定的利息收入，是一种低风险、低收益的投资品种；基金投资于众多股票，能有效分散风险，是一种风险相对适中、收益相对稳健的投资品种。</p>
    <p>9. 基金与银行储蓄存款有哪些区别？ </p>
    <p>性质不同：基金是一种受益凭证，基金管理人只是代替投资者管理资金，并不承担投资损失风险；银行储蓄存款是银行的一种负债，银行对存款者负有法定的保本付息责任；</p>
    <p>收益与风险大小不同：基金并不保证本金的安全，存在亏损的可能性，但也有机会分享证券市场上涨带来的收益；银行贷款利率相对固定，投资者几乎没有损失本金的可能性；</p>
    <p>信息披露程度不同：基金管理人必须定期向投资者公布基金的投资运作情况；银行不需要向存款人披露资金的运用情况。</p>
    <p>12.证券投资基金有哪些类型？</p>
    <p>按照运作方式不同，可分为开放式基金和封闭式基金。</p>
    <p>按照组织形式不同，可分为公司型基金和契约型基金。</p>
    <p>按照投资对象不同，可分为货币市场基金、债券基金、股票基金、混合基金。</p>
    <p>按照投资目标不同，可分为成长型基金、收入型基金和平衡性基金</p>
    <p>按照投资理念不同，可分为主动型基金和被动型基金</p>
    <p>19.什么是股票型基金？适合哪些人投资？</p>
    <p>股票基金是指以股票为主要投资对象的投资基金。根据相关规定，股票基金的股票资产投资占基金资产的至少60%以上。股票基金以追求长期的资本增值为目标，比较适合长期投资。</p>
    <p>股票基金属于高风险、高收益投资工具，适合风险承受能力较强的投资者投资。 </p>
    <p>20.为什么要投资股票型基金？</p>
    <p>股票市场是国家宏观经济的晴雨表，投资股票市场可以分享中国经济增长的成果；</p>
    <p>比起自己直接买股票，股票基金进行股票组合头投资，更容易让你获得经济增长的好处；</p>
    <p>随着中国股市的发展，上市公司也越来越多。即使作为一个专业投资者，也不可能有充足的时间和精力去关注所有的股票。基金管理公司通过先进的分析工具对各种信息进行科学地分析，从而提供专业的投资服务。 </p>
    <p>21.什么是债券基金？适合哪些人投资？</p>
    <p>债券基金是指以债券为投资对象的投资基金，根据相关规定，债券基金的债券资产投资占基金资产的至少60%以上。</p>
    <p>债券型基金的波动性通常要小于股票基金，使受益、风险适中的投资工具。适合追求稳定收入的投资者。</p>
    <p>22.为什么要投资债券型基金？</p>
    <p>投资债券型基金可以让你买到更“多”的债券，毕竟个人的钱是有限的，不可能卖很多种债券。</p>
    <p>投资债券基金可以让你获得专家理财的服务。</p>
    <p>投资债券基金可以让你的钱运用起来更灵活。相比投资于银行柜台交易的国债，通过债券基金投资于债券交易、赎回的费用都要低得多。</p>
    <p>23.什么是货币市场基金？适合哪些人投资？</p>
    <p>货币市场基金是主要投资于短期债券、中央银行票据、银行存款等低风险、高流动性金融工具的基金产品，该类基金购买的金融投资产品多为一年以内的各类短期货币市场工具，因此称为货币市场基金。
    与其他类型基金相比，货币市场基金具有风险最低、流动性最好的特点，其收益率一般要高于银行一年期定期存款，适合于厌恶风险、对资产安全性要求较高的投资者进行短期投资。所以，拥有一定金额的短期闲置资金，并希望有稳定增值机会的投资者，均适合投资货币市场基金。</p>
    <p>24.什么是混合型基金？适合哪些人投资？</p>
    <p>混合型基金是指投资于股票、债券以及货币市场工具的基金，且不符合股票型基金和债券型基金的分类标准。根据股票、债券投资比例以及投资策略的不同，混合型基金又可以分为偏股性基金、偏债型基金、配置型基金等多种类型。</p>
    <p>混合型基金的风险低于股票基金，预期收益则要高于债券基金，适合较为保守的投资者。</p>
    <p>25.什么是价值型基金？有哪些特点？ </p>
    <p>专注于价值型股票投资的股票基金被称为价值型股票基金。</p>
    <p>价值型基金随经济周期波动不大，属于风险较小、收益较小的基金</p>
    <p>26.什么是成长型基金？有哪些特点？ </p>
    <p>专注于成长型股票投资的股票基金被称为成长型股票基金。成长型基金属于风险程度最高、收益最大的基金，其重点并不着眼于股票的现价，而是预期未来股价表现会优于市场平均水平</p>
    <p>33.什么是保本基金？适合哪些投资者？</p>
    <p>保本基金，是指在基金产品的一个保本周期内（基金一般设定了一定期限的锁定期，在我国一般是3年，在国外甚至达到了7年至12年），对投资者的本金提供100%或者更高/低保证的基金，但若提前赎回，则不享受保本。保本基金在保本期内一般不接受申购，可以申购的部分资金也将不享受保本承诺，但可以赎回，因此保本基金也被称为“半封闭式基金”。</p>
    <p>保本基金适合风险承受能力比较弱的投资者投资或是在未来股市走势不确定的情形下投资，它既可以保障所投本金的安全，又可以分享股市上涨的收益，具有其特定的优势。</p>
    <p>34.保本基金有哪些特点？</p>
    <p>风险极低。由于保本基金的核心特点就是在投资者持有基金到期时可以获得本金保证，因此，投资者投资于保本基金就可以使本金免受损失；</p>
    <p>半封闭性。保本基金都会规定一个保本期，基金持有人只有持有到期才能获得保本的保证；而在保本期内赎回则不会获得保本保证，投资者不仅要自己承担基金净值波动的风险，而且还可能要支付较高的赎回费用，这种半封闭性使得保本基金较适合于以中长期投资为目标的投资人；</p>
    <p>增值潜力。保本基金在保证投资者本金安全的同时，通过股票或各种金融衍生产品的投资分享证券市场的收益。与银行存款或国债投资相比，保本基金具有较高的增值潜力，在同样保证本金回报的同时，具有较高的预期收益。</p>
    <p>一般基金交易要2个工作日交易确认后，才可以进行赎回操作。股票型基金也是一样的。</p>
