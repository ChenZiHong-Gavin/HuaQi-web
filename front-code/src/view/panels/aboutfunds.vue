          <h1 style="color:#F00">交易费用</h1>
          <p>申购费即申购基金时手续费；认购费即新基金认购时手续费；赎回费即赎回时手续费；</p>
          <p>特别说明一下申购费与认购费，它不是固定的，根据交易金额的大小会有差异，买的越多而越少。赎回费也不是固定的，一般来讲你持有的时间越长，赎回费越低。图片2中的费用详情中有详细说明，在购买每个基金时务必关注~</p>
          <p style="font-weight:900">申购费、认购费、赎回费是按照每次交易时一次性收取</p>
          <p>假设XX基金当天价格为1块钱/份，申购费0.8%，你准备购买1000份即1000块钱。</p>
          <p>申购费 = 申购金额 * 申购费率 = 1000元 * 0.8% = 8元</p>
          <p>净申购金额 = 1000元 - 8元（申购费） = 992元</p>
          <p>申购份额 = 净申购金额 / 当天价格 = 992元 / 1元 = 992份</p>
          <p style="font-weight:900">故，你用1000块钱买了992份，基金总资产992元</p>
          <p>假设持有的992份基金，基金赎回当日价格 1元/1份，赎回费0.05%</p>
          <p>赎回费 = 当日价格 * 持有份额 * 赎回费 = 1元 * 992份 * 0.05% = 0.496元</p>
          <p>实际到账金额 = 当日价格 * 持有份额 - 赎回费 = 1元 * 992份 - 0.496 = 991.504元</p>
          <h1 style="color:#F00">知识点补充：转换费计算</h1>
          <p>基金转换时产生一笔转换费，当从A基金转换到B基金时，一般要经过赎回A基金再申购B基金，过程中产生A赎回费与B申购费，但现在有一种的新的方式叫基金转换，即A基金直接转换为B基金，过程中产生A的赎回费与AB两只基金的申购费率补差费用，计算方式：基金转换费 = 转出基金赎回费 + 两个基金申购费补差</p>
