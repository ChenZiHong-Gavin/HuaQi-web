            <h1 style="color:#F00">指数型基金</h1>
            <img src="../../assets/images/image5.jpg">
            <p>指数基金分成指数和基金两个部分。“指数”就像养鸡场有一个大篮子，股票就相当于各式的鸡蛋，指数会根据自己的标准，选择不同类型的鸡蛋放入篮子里，现实中例如上证50，就是上海交易所中选择最具代表作50只股票组成。而“基金”就相当于基金公司拿着自己的小篮子，去大篮子里挑选鸡蛋，大篮子里有50％乌鸡蛋，基金公司就往自己的小篮子放50％的乌鸡蛋；大篮子里有20％的笨鸡蛋，基金公司也往自己的小篮子放20％的笨鸡蛋，不用浪费精力纠结于如何选择，按照模板照搬就行，这就是指数基金。</p>
            <p>我们常见的指数有沪深300指数，上证50指数，深圳50指数等等，其实就是选取了不同市场中具有代表性的n只股票，通过一些算法把它们编织成一个指数，来代表某个市场的表现。所以投资于指数的基金，本质上就是投资于这些指数所包含的股票的基金。因此，指数型基金的表现，基本上就是与该指数的表现一致。</p>
