            <p>第一阶段，股价很低，发行人的违约风险比较高，如同一个困境债券，价格上涨很快；</p>
            <p>第二阶段，股价也还相对较低，转股的可能性不大，可转债的价值接近普通债券；</p>
            <p>第三阶段，随着股价上涨，转股的期权逐渐被执行，这是套利者比较喜欢的阶段，因为它可以获得转债带来的价值。</p>
            <p>第四阶段，股价很高，可转债基本可以当做股票使用，其对于股票的敏感性也变得很高。</p>
            <p> </p>
            <p style="font-weight:900">6.4 风险管理</p>
            <p>根据组成成分，主要分为债券的风险和期权的风险，其中债券风险包括：</p>
            <p>久期：描述了债券价格变化对利率变化的敏感性；</p>
            <p>凸性：表示久期对利率变化的敏感性，或者说是价格对利率变化的二阶导数；</p>
            <p>信用敏感性：久期的近似值，表达的含义相同。</p>
            <p>期权风险包括：</p>
            <p>Delta：股票价格对可转债价格的敏感性；</p>
            <p>Gamma：Delta对价格的敏感性，相当于股票价格对可转债价格的二阶导数；</p>
            <p>Vega：可转债价格对股价波动性的敏感性；</p>
            <p>Theta：时间延迟性，可转债价格随着时间变化的速度；</p>
            <p>Rho：可转债价格对利率变化的敏感性。</p>
            <p> </p>
            <p style="font-weight:900">6.5 Delta对冲</p>
            <p>假设某时刻，可转债A的价格为1052.11元，所对应的普通债券的价格为1000元，那么这样一种错误的定价应该如何把握？低价持有可转债直到市场价格恢复到合理水平并不是一个好的套利策略，因为在这个过程中，可转债多头会面临许多可能吞噬收益的风险。最好的办法是买入可转债，在持有过程中动态地对冲风险，直到错误定价的出现，这就是Delta对冲的基本想法。</p>
            <p>持有可转债多头头寸的过程中，利率风险可以通过卖空利率期货或者利率互换来对冲；而股票价格下跌的风险可以通过卖空一定数量的股票来对冲，这个数量就可以用delta值来确定。例如，假设股票价格上升1元，可转债的价格就会上升delta元，或者说是delta份股票和1份可转债刚好完全对冲，数学上这就是可转债价格对股票价格的一阶导数，如图所示：</p>
            <p> </p>
            <p>随着股价上涨，delta增大，为了对冲风险，套利者需要卖空更多的股票，并且需要不断地调整股票仓位。实践过程中，调整的频率会根据固定的时间间隔或者固定的价格变动来确定，比如每小时或者价格每变动1元，调整一次。</p>
            <p>可转债一个有趣的性质，是它的高凸性或高Gamma值，意味着股票价格上涨，可转债涨幅超过股价，股票价格下跌，可转债跌幅小于股价，无论什么情况，理论上都可以取得正收益。</p>
            <p>这里说的是理论情况。而实际上，可转债中期权的时间价值会不断降低，时间会消除凸性带来的价值，最终在动态对冲情况下的预期收益率将会等于无风险收益率。上图中，pnl曲线的形状是由股票的实际波动率和可转债的隐含波动率的关系决定的：如果实际波动率大于隐含波动率，delta对冲头寸的收益率将会超过无风险利率；如果实际波动率小于隐含波动率，delta对冲头寸的收益率将会小于无风险利率。</p>
            <p style="font-weight:900">6.6 剥离和互换</p>
            <p>利率风险和股价风险都可以通过一定方式对冲，而信用差价风险并没有被消除，而这种风险其实很重要，因为许多可转债都是投资级以下的，风险较大。卖空股票、卖空债券、使用信用违约互换等方式，都会造成新的更大的风险。在这种情况下，金融中介机构创造了一种叫做“资产互换”的新的工具，这种金融工具把可转债剥离成固定收益和股票期权两部分，固定收益部分由“信用购买者”一方持有，而股票期权部分由“信用出售者”持有。</p>
            <p>第一步，对冲基金经理买入可转债，卖空股票，此时，他承担了包括利率升高和信用差价升高带来的风险。</p>
            <p>第二步，基金经理与信用投资者进行互换交易。首先，基金经理将可转债低价卖给信用投资者，这个价格是由债券的未来现金流按照(LIBOR + 固定信用差价)的折现值计算；然后，为了补偿这个折扣，信用投资者给基金经理一个场外看涨期权，允许他以一个固定价格回购可转债。这个价格是由债券的未来现金流按照(LIBOR+ 固定召回差价)的折现值计算，这个召回差价小于之前提到的信用差价，这是为了防止频繁交易。</p>
            <p>如此交易之后，双方都获得了他们想要的风险暴露：对冲基金仍然因为持有看涨期权，仍然可以获得股票上涨的收益，而不再暴露于信用差价风险；信用投资者持有可赎回债券，单向押注于债券的信用等级提高，面临信用风险和利率风险，不受股票涨跌的影响。实践过程中，资产互换有许多形式，但是基本的实现原理都是相同的：把可转债剥离成两个部分，可转债套利者通过股票的风险产生收益，对冲了信用和利率风险。</p>
            <p> </p>
            <p style="font-weight:900">7. 固定收益套利策略</p>
            <p>固定收益市场是对冲基金争夺的一片肥沃土地，主要基于几个原因：1、缺少一种标准的定价模型；2、对于各种固定收益工具之间存在的多种相对价格；3、特殊资产价格的非理性但是可以预测的供需关系；4、有些固定收益证券复杂的结构。以上几个因素导致固定收益证券的多种投资策略，这些策略主要可以分为以下三类：相对价值、市场中性、和方向交易。</p>
            <p> </p>
            <p style="font-weight:900">7.1 基本工具</p>
            <p>对于固定收益套利而言，最基本的工具就是利率期限结构，或者说是时间的价值，它是利率和到期时间之间的关系。大多数债券都是付息的，对于这类付息债券的期限结构，定义为收益率曲线，它和零息债券的期限结构略有差异：</p>
            <p>收益率曲线的作用主要体现在以下几个方面：</p>
            <p>1、作为一些债券收益率评价的基准</p>
            <p>2、对于未来收益率的预测</p>
            <p>3、比较不同到期日的收益率</p>
            <p>4、对债券进行定价</p>
            <p> </p>
            <p style="font-weight:900">7.2 策略介绍</p>
            <p>固定收益套利策略大量地基于数学和统计估值模型。对于利率敏感的证券，价格会随着收益率曲线、预期现金流、信用评级、波动率曲线的变化而变化，也因此产生了许多动态的，复杂的套利策略。</p>
            <p>国库券剥离：将付息国库券剥离成票息和零息债两部分，其中票息的部分以一个更高的价格卖给投资者（如养老基金），从中赚取差价。</p>
            <p>息差交易：做多高收益债，做空低收益债。例如，对于一个上涨的收益率曲线，可以买入长期债券，借入并卖出短期债券，赚取两个利率之间的差价。</p>
            <p>收益率曲线套利：在曲线上不同的点分别建立多头和空头头寸，从曲线将来一些可能的变化中获取收益。比如，在一些异常情况下，30年国债的收益率低于10年国债，也就是前者的价格偏高，后者价格偏低，这时候可以做空30年国债，做多10年国债，等到二者价格恢复合理区间之后，收获这个差价。</p>
            <p>互换价差套利：利率互换价差由经济基本面和一些金融变量，以及交易员的套利行为决定。如果互换差价高于一个基本水平，一个预期价差降低的交易员则会进入利率互换的多头头寸（表示接受固定利率），做空国库证券。</p>
            <p>国库券-欧洲美元价差（TED）：这是美国国库券收益率和欧洲美元收益率的差异，表明了投资者对于美国国库和国际银行相对质量好坏的观点。许多对冲基金就是基于对TED差价走势的判断来进行交易。假设他们预期TED差价变大，做多国库券期货，做空欧洲美元期货，反之同理。</p>
            <p> </p>
            <p style="font-weight:900">8. 新兴市场策略</p>
            <p>新兴市场的投资最早源于80年代中期世界银行的国际投资公司（IFC），协同几家有兴趣的保险公司和养老公司，一起募集的5千万美元的种子基金。它们的投资覆盖了32个国家，覆盖大约670亿美元的市值，当时占据了全球市场的2.5%。自此之后，对于新兴市场的投资被越来越多的关注，这主要也是源于这些国家的快速发展，不断健全的货币、财政政策和经济基础。</p>
            <p> </p>
            <p style="font-weight:900">8.1 新兴市场的投资机会</p>
            <p>目前新兴市场的市值在全球市场所占比例为7.5%，MSCI新兴市场指数覆盖了26个国家。“新兴”一词主要体现了这些国家由生产率提升、科技创新、      经济体制改变所带来的高速经济增长；另一方面，也表现在政治的不稳定性、货币政策不健全以及较高的外债。尽管如此，庞大的市场规模和人口基数，还是让这一市场吸引了诸多投资者的兴趣。</p>
            <p>新兴市场的投资往往带有较大的风险，通过更大的波动性来体现，并且这种波动用指数化配置的方式并不是最好的投资方式。主要有三个原因：1、指数主要用于分散风险而不是挑选优质个股，但是新兴市场的分散效果不好，主要因为在市场危机的情况下，股票之间会变得高度相关，同时成分股的集中度很高，分散化的投资并不能降低整体风险。2、新兴市场的一些机制使得编制指数十分困难，比如一些证券缺少流动性、高昂的交易成本、一些政治风险等。3、新兴市场高度无效。</p>
            <p>基于这些原因，指数化的投资并不是最好的选择。事实上，这样一种波动率大、相对无效的市场，为主动管理策略提供了很好的环境，准确把握市场无效性和异象，往往可以带来比成熟市场更大的收益。</p>
            <p> </p>
            <p style="font-weight:900">8.2 股票投资策略</p>
            <p> </p>
            <p>新兴市场的股票投资往往采用自下而上的分析方法，高波动率会带来短期的错误定价，这对于投资者而言是一件好事。他们持续地在市场上寻找那些具有较大折价的股票买入，然后以一个较高的溢价卖出。当然，对于多空策略而言会造成偏多头，这不仅是因为多空头寸市值的变化，还包括了做空工具受限等原因。对于股票选择的过程，通常同样也是基于质量（尽职调查）和数量（基本面指标）；有的基金经理关注一个市场，有的则可以在多市场间分配权重。</p>
            <p>近期的一些新兴市场对冲基金还采用比较激进的策略，将目标锁定在那些大型公司进行兼并收购，类似于传统的私募股权投资做的事。这一方面给新兴市场的公司带来了一定改革的压力，另一方面，这些门口的野蛮人也让整个市场的参与者建立更为有效的防御措施，这是资本市场走向成熟的必经之路。</p>
            <p style="font-weight:900">8.3 固定收益策略</p>
            <p> </p>
            <p>历史上，新兴市场国家的主权债经常周期性地在一波华丽的业绩之后，无法避免地遇到流动性危机、波动异常、市场暴跌、最终违约。这些违约最终都以付出高额的拯救费用收场，这样一个过程周而复始。</p>
            <p>如今的固定收益市场主要包括三种工具：1、布兰迪债券；2、欧洲债券；3、本土债券。这些工具大多数的评级是低于投资级的，因此也面临着利率风险、主权风险以及汇率风险，这三种风险是高度相关的。对冲基金经理在选择固定收益工具时，一般也采用自上而下的方式，国家选择、证券选择和风险管理。有的基金经理关注短期，尤其是新兴市场价差的变化；有的关注中期的风险溢价和持有期收益；有的关注长期，采用更激进的方式，主要关注违约证券。</p>
            <p>新兴市场一个最大的问题是市场传染，尤其对于固定收益证券，不仅受到自身市场的影响，而且受到周边新兴市场以及外来资本的影响。因此对于投资者而言，这类市场的危机难以预测，分散化的投资在真正的危机到来之时，也很难发挥作用。</p>
            <p> </p>
            <p style="font-weight:900">9. 全球宏观策略</p>
            <p>全球宏观投资起源于1980年代，由当时的多空对冲和管理期货策略的风格漂移演变而成。那个时代的多空策略主要采用自下而上的分析方法，投资于那些未被充分研究的小盘股，并取得了很好的业绩。随着市场规模的增加，这些资金也在寻找新的流动性更好的市场，比如George Soros的量子基金，还有Julian Robertson的老虎基金。另一方面，管理期货基金往往投资于衍生品和期货市场，本身也是基于全球化的投资，比如Louis Moore Bacon的Moore资本，Paul Tutor Jones 的Tudor投资。逐渐地，这两类策略的投资风格越来越接近，形成了目前基于全球市场和动态资产配置为主的投资理念。</p>
            <p> </p>
            <p style="font-weight:900">9.1 投资方法论</p>
            <p> </p>
            <p>全球宏观策略很难被准确定义，因为每种策略都不一样，并且都是主观的和基于市场机会的，主要包括了两个特点：1、策略是基于全球化的，包括了在各个流动性好的市场分析其趋势、市场偏差、经济周期和特殊地区的结构性变化，以期获得收益；2、主要关注结构化的宏观经济平衡并且发现宏观趋势。</p>
            <p>分析宏观经济的方法主要分为三类：基于反馈的方法、基于模型的方法和基于信息化的交易方法。</p>
            <p> </p>
            <p style="font-weight:900">9.2 基于反馈的方法</p>
            <p> </p>
            <p>主要关注于市场心理，发掘市场参与者偏离理性的一些情况。大多数是时候市场都是理性的，因此这样的机会并不多，但是在少数情况下，市场的价格会受到行为偏差的影响。例如，当投资者非常容易地赚到钱，会更快地买入；或者是在短时间内大量损失时，会匆忙地卖出。基于反馈的方法就充当这类人群的对手方，他们敏锐地察觉到这些恐惧、贪婪、狂热，然后准确地在市场底部进入，在泡沫破裂之前退出。</p>
            <p> </p>
            <p style="font-weight:900">9.3 基于模型的方法</p>
            <p>这是通过数据和复杂的宏观经济学模型来做市场预测。模型计算的结果和隐含的市场预期往往不一致，这就存在投资的机会。当然，这样的机会很短暂，因此基金经理需要不断的从最新的学术论文中获取灵感，更新和完善模型。</p>
            <p> </p>
            <p style="font-weight:900">9.4 基于信息化的交易方法</p>
            <p> </p>
            <p>这种方法高度依赖数据和计算机程序，他们在微观的领域（中央银行发布的信息、调研数据、信心指数、流动性指标、预测机构、政治评论等）收集、整理和分析大量的数据，来形成投资观点。支撑这一方法的逻辑是，微观层面的变化往往快于宏观层面，但是数据十分分散，需要大量的分析工作来挖掘有用信息。主流的信息化交易方法还是主观判断，一旦出现较高取胜概率的点位，交易就会被自动执行。进场和离场的点位往往由基本面和基本面的分析结果共同决定。</p>
            <p> </p>
            <p style="font-weight:900">10. CTA策略</p>
            <p>现代的期货交易源于19世纪中期美国芝加哥期货交易所，当时的市场参与者主要包括了对冲者和投机者。前者主要通过期货合约来对冲现货商品价格波动的风险，而后者主要通过对未来价格的预测来取得资本收益。1970年初，一种新的参与者出现了，他们聚合了一批以期货合约为交易标的的基金经理，又称为商品交易顾问（CTA）。但是当时他们所募集到的资金非常有限，因为投资者们都不懂期货交易的机会和风险，也不懂期货投资。</p>
            <p>1970年末期，当时金融市场出现了一些不确定性，许多交易所推出了新的期货合约，比如利率期货、债券期货、股票期货等，期货投资也因此开始被广泛接受。由于绝大多数的个人和公司并不具备专业的期货投资知识，他们聘请了许多专业的交易员来为他们进行投资，CTA的名义也因此得来。从那之后，CTA策略的管理规模就在逐年增加。</p>
            <p> </p>
            <p> </p>
            <p style="font-weight:900">10.1 交易风格</p>
            <p>根据交易方法，可以分为主观交易和系统化交易；根据分析方法，可以分为基本面分析和技术面分析；根据收益来源，可以分为趋势追踪和非趋势追踪；根据时间窗，可以分为短期、中期和长期。</p>
            <p  style="font-weight:900">10.2 主观交易 vs 系统化交易</p>
            <p>主观交易是最早出现的方式，就是凭借交易者对于市场主观的判断来进行买卖，决策的过程和全球宏观策略类似。随着技术进步，金融数据量的增加，越来越多的CTA逐渐淘汰人为主观的判断，改用系统性交易的方式。这种交易方式大量地依赖计算机软件，通过分析市场价量等指标，进行买入和卖出的操作。</p>
            <p>如今，大约有80%的期货交易都是通过系统化的方法进行的，但是市场对于两种方式的优劣，仍然存在很大争议。系统化的交易避免受到人为情绪的影响，交易过程很稳定。同时，系统化交易可以通过扩大交易覆盖面，来更好地降低风险，同时增加了对于业绩预测的准确信。另一方面，主观交易员认为系统化的交易缺乏经济学基础，对于结果不容易解释，它只是用历史数据来进行拟合，对未来的预测效果很有限。</p>
            <p> </p>
            <p style="font-weight:900">10.3 基本面分析 vs 技术面分析</p>
            <p>基本面分析是通过经济、政治、环境等相关的基本面因子来预测市场的内在价值，通过对内在价值和当前市场价格的判断，从而进行的方向性交易。而技术面主要通过分析市场信息，来预测价格的走势。技术分析者认为资产收益之间是具有统计学的相关性的，也就是历史趋势将会重演。他们通过大量的价量指标来需找可能的模式和相关性，他们并不在意基本面的变化。这种方法的理论基础是市场更多地受到行为和心理因素方面的影响，因此情绪仍然是投资者的主要构成。</p>
            <p> </p>
            <p style="font-weight:900">10.4 趋势追踪 vs 非趋势追踪</p>
            <p>趋势追踪是大多数基金经理的选择，这个策略的核心思想就是动量。例如当价格朝一个方向运动，那么它在下一个阶段还是很有可能继续朝这个方向变化。因此趋势追踪策略一般依据量化模型来进行技术分析，从而发现市场趋势。非趋势追踪则相反，他们关注那些趋势快速反转的时机，来建立和趋势变化相反的头寸。这种策略的时机选择和风险管理尤其重要，以免因持续的价格动量而爆仓。</p>
            <p> </p>
            <p style="font-weight:900">10.5 时间窗</p>
            <p>根据不同的投资风格，时间窗的长短也不一样。比如，对于趋势追踪策略，短期为几小时到几天，中期为几天到30天，长期为2到3个月；而对于非趋势追踪者，时间窗一般很短，只有在机会出现的时期进行交易。时间窗的选择是会影响到策略表现的，比如短期策略就需要更低的费用，高流动性和自动化交易。</p>
