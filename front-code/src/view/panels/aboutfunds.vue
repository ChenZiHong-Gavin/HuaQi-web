            <p>净值不仅是每单位基金的价格，也代表着一个基金的历史收益！</p>
            <p style="font-weight:900">2.1、单位净值 VS 累计净值</p>
            <img src="../../assets/images/image72.jpg">
            <p>部分基金第三方平台里，展示着单位净值和累计净值，且两个净值不一样，为什么呢？单位净值就是每一个单位基金的价值；累计净值其实是单位净值加上历史的所有分红。基金的累计净值就是单位净值，但是该基金进行过分红或拆分导致了不同。</p>
            <p>分红指基金获得收益后以现金或者份额的方式分给你，举个例子，你持有A基金1000份，当日净值1.3元，A基金进行分红，每份分红0.3元，那么你最后可以得到300块大洋分红，相对应的基金净值会变成1元，而累积净值仍是1.3元。</p>
            <p>拆分指一个基金进行拆分，原净值为2元，1份分为2份，拆分后净值变成1元，累积净值还是2元。</p>
            <p style="font-weight:900">2.2、净值估算</p>
           <img src="../../assets/images/image73.jpg">
           <p>净值估算就涉及到了基金清算的事了，一个基金中包含了大量的投资品即投资标，当一个投资品10块钱购入，涨幅10%，也就是当前价格是11元，很好算，但麻烦就在投资标有很多很多的时候，基金公司就无法做出实时计算了。一般会在每日收盘后进行统一清算，那么净值估算就容易理解了，用一种粗略的估算方式来展现有可能的预期而已，其实我个人认为这个没什么大用，只是让你在第一时间进行参考而已。</p>
           <p style="color:#87CEFA">补充知识：细心的你会发现，货币基金没有净值表现的，而是用万份收益和七日年化来展现。货币基金与其他不同，它的净值永远是1元，变得只是份额。</p>
