            <p>唐建成为中国内幕交易获罪第一人，更引发了《刑法》的修订，此后“老鼠仓”最高可处5年以上10年以下有期徒刑。</p>
            <p>但这个套路不会结束的，未来还有前仆后继的王黎敏、张野、涂强、刘海、韩刚、许春茂、郑拓、马乐……</p>
            <p>每一次牛市，海妖都会歌唱，招引你的心魔。</p>
            <p>“老鼠仓”事件虽然给基金行业抹了黑，但是不要紧，赚钱效应能平复一切伤痛。</p>
            <p>2007年的巅峰牛市，使基金行业真正走进千家万户，奠定了其在中国资产管理行业的一席之地。而随后迅雷不及掩耳的暴跌，又把追高的基民狠狠套牢，成了其股票型基金最稳定的持有者！</p>
            <p>2007年之前，中国有60家基金公司。这是一条分水岭，自此，基金行业有“60前”、“60后”一说。</p>
            <p>所谓“60前”，就是见过2007年牛市的，股票型基金有铁杆套牢投资者的，每年管理费有保障的人家。而“60后”，就是没见过2007年牛市的，一进场，全国韭菜都被割完了，天天得挣扎图存、艰难糊口的人家。</p>
            <p>2007年，还出现了我最不喜欢的创新产品——分级基金，以及冲出国门，给老外接盘的QDII基金。</p>
            <p>中国的私募基金也是在这一年崛起的，虽然还没有法律地位。</p>
            <p>2008年到2012年，对基金来说，都算不得什么好日子。行业整体规模5年都没有增长（均低于2007年），管理费最高的股票基金、混合基金规模还在下行，债券基金和货币基金规模虽然增长，但管理费较低，往往不足以弥补损失。</p>
            <p>那几年发年终奖金的时候，我们做债的人真是要郁闷死了：如果股票涨得好，公司赚了钱，可不是我们赚的，自然没我们的份儿；如果债券涨的好，我们虽然赚了钱，但公司没赚着啊，大家都没份儿。</p>
            <p>公司想做大规模，就不断发新基金，新基金数量越来越多，规模却越来越小，基金经理一拖多（一名基金经理管理多支基金）成了常态。研究员升任基金经理速度很快，但基本功却越来越没时间夯实了。</p>
            <p>这样熬着熬着，到了2012年末，已经有不少中小基金公司就要活不下去了……</p>
            <p>咋个办？</p>
            <p>基金业第三个周期叫“自由化时期”</p>
            <p>（2012.12.28~2018.7.19，约6年）</p>
            <p>启动标志仍旧是——改革春风吹满地！</p>
            <p>2012年12月28日，十一届全国人大常委会第三十次会议通过了修订版《中国人民共和国证券投资基金法》（2013年6月1日起施行）。</p>
            <p>在这款法规的前前后后，其实还伴随着一系列的放松政策。</p>
            <p>《证券投资基金管理公司管理办法》将基金公司成立的门槛儿降低了；</p>
            <p>《关于深化基金审核制度改革有关问题的通知》把基金审核速度加快了；</p>
            <p>《基金管理公司特定客户资产管理业务试点办法》将基金专户的投资限制放松了；</p>
            <p>最后，还有一份出乎意料的大礼——《证券投资管理公司子公司管理暂行规定》给了基金公司一张万能牌照——子公司。</p>
            <p>tips</p>
            <p>哦，新《基金法》还顺道儿承认了私募基金的法律地位。</p>
            <p>当年最好做的业务，叫“影子银行”。就是帮助银行把钱变相贷款给企业。</p>
            <p>你问为什么银行不自己直接贷？</p>
            <p>当然是有为难之处啦，或者资本金限制，或者贷款额度限制，或者企业所在行业受限。总之，这事儿得绕一道，假手一家非银金融企业，但也不是谁家都有牌照能做的。</p>
            <p>银行原本的小弟，只是信托。</p>
            <p>信托业也是个命运多舛的行业啊，2001年之前被整治了五次，从1000多家，整到59家。但自从2009年抱住银行的大腿，嚯嚯嚯嚯，一顿操作猛如虎，转眼成为中国仅次于银行管理规模的第二大金融机构。</p>
            <p>如今，基金子公司也能来分它一杯羹了。（与此同时，券商资管也可以分一杯了。）</p>
            <p>可以说2013、2014年，子公司赚的钱养活了不少濒死的中小基金，给了他们喘息着发展公募的机会，但2015年之后子公司捅的篓子陆续浮出水面，打击也是不小。那是另一个生死时速的故事了，我们按下不表。</p>
            <p>且说说第三周期里基金业最重大的变化——货币基金的崛起！</p>
            <p>基金业有个逆风翻盘的传奇——天弘基金。</p>
            <p>在“阿里”出手“盘”它之前，天弘基金自己发展了7~8年，规模不足100亿，然而在2013年6月，对接天弘货基的“余额宝”横空出世后，天弘基金的总规模一年间涨到将近2000亿，两年快6000亿，到2017年，快18000亿了！</p>
            <p>一举成为基金公司总规模NO.1!</p>
            <p>但只要大家看一下天弘家货币基金的占比，就会发现一直在95%以上，其他40多支基金加一块儿还不到400亿。</p>
            <p>这也挺是尴尬的~我们知道，货币基金有软保本约束，真要出点什么事儿，公司多少年赚的钱都不一定够赔的。</p>
            <p>2018年，为什么天弘基金的规模下降了5000多亿呢？</p>
            <p>那是因为余额宝为了分散风险，从2018年6月开始，除天弘货基外，又对接了13支不同公司的货币基金，致使这些产品在几个月里纷纷实现了成百到上千亿的增长。</p>
            <p>余额宝和天弘，固然是难以复制的神话，但货币基金的兴盛却是一场水到渠成。</p>
            <p>它的根本在于金融压抑。</p>
            <p>广大老百姓无法分享货币市场的收益，在短期资金腾贵的情况下，仍然只能把自己的闲钱放在银行里吃0.35%的活期利息。</p>
            <p>但货币基金要抢银行的生意却不容易，毕竟活期存款随时可取，随时可刷卡支付。而早年的货币基金，办理赎回后，要4~7个工作日才能到账，再从理财账户划到存款账户，才能使用银行卡支付。</p>
            <p>2012年10月11日，南方、国泰、汇添富推出“货币基金T+0当日赎回方案”获得证监会批准。能T+0，不是货基的效率提高了，而是靠托管行或者股东垫付，但不管怎么说，此举为货基PK活期存款带来了技术性突破。</p>
            <p>2013年，又一个钱荒，银行间7天回购加权利率都能飙到10%以上了！</p>
            <p>此时，余额宝顺天应命而出了，带着淘宝的巨大客源，支付宝的超级便利支付功能，阿里爸爸按秒到账的强大资金支持，再加上中国货币市场提供的，为打击非标才出现的畸形高收益率。</p>
            <p>7%的类活期存款见过没有？！——它就是“余额宝”。</p>
            <p>瞬间炸了！</p>
            <p>余额宝让货币基金这一概念深入人心后，其他基金公司也开始获益。</p>
            <p>在基金行业第一周期，先入为主的“股票基金”无疑是主流；而进入第二周期，被熊市坑聪明了的基民们更倾向于投资能灵活切换股债持有比例的“混合基金”；到了第三周期，久久被压抑的短期理财需求爆发了，货币基金占比最终超过了60%。</p>
            <p>货基的崛起成了全行业的盛宴。</p>
            <p>2014年中至2015年6月12日，第三周期的股票牛市也来了！债券牛市还在！货币基金不断放量。其他什么定增、打新、量化、通道无一不是热火朝天。</p>
            <p>这正是秦可卿说的“烈火烹油，鲜花着锦之盛”。</p>
            <p>但其下暗流涌动却是最高的杠杆，最复杂的通道，最随意承诺的保本保收益，和真正到来的中国债市违约潮。</p>
            <p>所以，第三次看到盛宴散场，一地鸡毛，同志们应该没有什么可意外的了吧？</p>
            <p>2015年中，股市凉凉。</p>
            <p>2016年底，债市凉凉。</p>
            <p>2018年4月27日，《关于规范金融机构资产管理业务的指导意见》（俗称“资管新规”）出台，整个资管行业都感觉要凉凉了。</p>
            <p>但所谓周期就是这样，周而复始，否极泰来……</p>
            <p>基金业第四个周期叫“规范化时期”</p>
            <p>（2018.7.20~？）</p>
            <p>姿势照旧，改革春风吹满地——</p>
            <p>2018年7月20日，人民银行发布《关于进一步明确规范金融机构资产管理业务指导意见有关事项的通知》，对4月27日发布的《关于规范金融机构资产管理业务的指导意见》进行了最宽松的解读。</p>
            <p>2018年9月28日，银保监会正式出台《商业银行理财业务监督管理办法》，12月2日，又出台《商业银行子公司管理办法》，给银行续了口气，就是给基金行业的上游钱袋子续了口气啊。</p>
            <p>2018年10月22日，证监会出台《证券期货经营机构私募资产管理业务管理办法》及《证券期货经营机构私募资产管理计划运作管理规定》，重新给包括基金专户，基金子公司在内的所有私募资产管理业务统一了标准，划出了道道儿。要知道在此之前，专户和子公司都被处罚懵了，业务无心做，人才四飘零，风控动辄惊。</p>
            <p>终于，又一场新的征程开启了……</p>
